    async def propose_change(
        self,
        evolution_type: EvolutionType,
        file_path: str,
        new_code: str,
        description: str,
        source: str,
        confidence: float = 0.5,
    ) -> CodeChange:
        """Propose a code change for review and validation"""
        # Read original code
        full_path = self.project_root / file_path
        original_code = ""
        if full_path.exists():
            with open(full_path, 'r', encoding='utf-8') as f:
                original_code = f.read()
        
        change_id = hashlib.md5(
            f"{file_path}:{new_code}:{datetime.now().isoformat()}".encode()
        ).hexdigest()[:12]
        
        change = CodeChange(
            id=change_id,
            evolution_type=evolution_type,
            file_path=file_path,
            original_code=original_code,
            new_code=new_code,
            description=description,
            source=source,
            confidence=confidence,
        )
        
        with self._lock:
            self.pending_changes[change_id] = change
            self.stats['changes_proposed'] += 1
        
        # Auto-validate if enabled
        if self.auto_validate:
            await self.validate_change(change_id)
        
        logger.info(f"Change proposed: {change_id} - {description}")
        
        return change
    
    async def validate_change(self, change_id: str) -> Dict[str, Any]:
        """Validate a proposed change"""
        if change_id not in self.pending_changes:
            return {'valid': False, 'error': 'Change not found'}
        
        change = self.pending_changes[change_id]
        change.status = EvolutionStatus.VALIDATING
        
        results = {
            'syntax_valid': False,
            'imports_valid': False,
            'no_dangerous_code': False,
            'tests_pass': False,
            'overall_valid': False,
        }
        
        try:
            # Check syntax
            ast.parse(change.new_code)
            results['syntax_valid'] = True
        except SyntaxError as e:
            results['syntax_error'] = str(e)
        
        # Check for dangerous patterns
        dangerous_patterns = [
            r'os\.system\s*\(',
            r'subprocess\.(?:call|run|Popen)\s*\(',
            r'eval\s*\(',
            r'exec\s*\(',
            r'__import__\s*\(',
            r'open\s*\([^)]*["\']w["\']',  # Writing to files
            r'shutil\.rmtree\s*\(',
            r'os\.remove\s*\(',
        ]
        
        results['no_dangerous_code'] = True
        for pattern in dangerous_patterns:
            if re.search(pattern, change.new_code):
                results['no_dangerous_code'] = False
                results['dangerous_pattern'] = pattern
                break
        
        # Check imports
        results['imports_valid'] = self._validate_imports(change.new_code)
        
        # Run tests if required
        if self.require_tests:
            results['tests_pass'] = await self._run_tests(change)
        else:
            results['tests_pass'] = True
        
        # Overall validation
        results['overall_valid'] = all([
            results['syntax_valid'],
            results['imports_valid'],
            results['no_dangerous_code'],
            results['tests_pass'],
        ])
        
        change.validation_results = results
        
        if results['overall_valid']:
            change.status = EvolutionStatus.APPROVED
        else:
            change.status = EvolutionStatus.REJECTED
            with self._lock:
                self.rejected_changes.append(change)
                del self.pending_changes[change_id]
                self.stats['changes_rejected'] += 1
        
        return results
    
    def _validate_imports(self, code: str) -> bool:
        """Validate that all imports are available"""
        try:
            tree = ast.parse(code)
            
            for node in ast.walk(tree):
                if isinstance(node, ast.Import):
                    for alias in node.names:
                        try:
                            importlib.import_module(alias.name)
                        except ImportError:
                            return False
                elif isinstance(node, ast.ImportFrom):
                    if node.module:
                        try:
                            importlib.import_module(node.module)
                        except ImportError:
                            return False
            
            return True
        except Exception:
            return False
    
