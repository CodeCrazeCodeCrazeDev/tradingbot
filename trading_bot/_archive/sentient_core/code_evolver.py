    async def apply_change(self, change_id: str) -> bool:
        """Apply an approved change"""
        if change_id not in self.pending_changes:
            logger.error(f"Change {change_id} not found")
            return False
        
        change = self.pending_changes[change_id]
        
        if change.status != EvolutionStatus.APPROVED:
            logger.error(f"Change {change_id} not approved")
            return False
        
        # Create backup
        self._create_backup(change)
        
        try:
            # Apply the change
            full_path = self.project_root / change.file_path
            full_path.parent.mkdir(parents=True, exist_ok=True)
            
            with open(full_path, 'w', encoding='utf-8') as f:
                f.write(change.new_code)
            
            change.status = EvolutionStatus.APPLIED
            change.applied_at = datetime.now()
            
            with self._lock:
                self.applied_changes.append(change)
                del self.pending_changes[change_id]
                self.stats['changes_applied'] += 1
            
            logger.info(f"Change {change_id} applied successfully")
            return True
            
        except Exception as e:
            logger.error(f"Failed to apply change {change_id}: {e}")
            self._rollback_change(change)
            return False
    
    def _create_backup(self, change: CodeChange) -> None:
        """Create a backup before applying change"""
        if not change.original_code:
            return
        
        backup_dir = self.backup_path / change.id
        backup_dir.mkdir(parents=True, exist_ok=True)
        
        backup_file = backup_dir / "original.py"
        with open(backup_file, 'w', encoding='utf-8') as f:
            f.write(change.original_code)
        
        # Save metadata
        meta_file = backup_dir / "metadata.json"
        with open(meta_file, 'w') as f:
            json.dump(change.to_dict(), f, indent=2)
        
        # Clean up old backups
        self._cleanup_old_backups()
    
    def _cleanup_old_backups(self) -> None:
        """Remove old backups beyond max limit"""
        backups = sorted(
            self.backup_path.iterdir(),
            key=lambda p: p.stat().st_mtime
        )
        
        while len(backups) > self.max_backups:
            oldest = backups.pop(0)
            shutil.rmtree(oldest)
    
    def _rollback_change(self, change: CodeChange) -> bool:
        """Rollback a change to its original state"""
        try:
            full_path = self.project_root / change.file_path
            
            if change.original_code:
                with open(full_path, 'w', encoding='utf-8') as f:
                    f.write(change.original_code)
            elif full_path.exists():
                os.remove(full_path)
            
            change.status = EvolutionStatus.ROLLED_BACK
            self.stats['rollbacks'] += 1
            
            logger.info(f"Change {change.id} rolled back")
            return True
            
        except Exception as e:
            logger.error(f"Rollback failed: {e}")
            return False
    
