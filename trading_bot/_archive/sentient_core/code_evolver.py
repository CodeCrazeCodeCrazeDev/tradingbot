    async def optimize_code(
        self,
        file_path: str,
        optimization_type: str = "performance",
    ) -> Optional[CodeChange]:
        """Optimize existing code"""
        full_path = self.project_root / file_path
        
        if not full_path.exists():
            return None
        
        with open(full_path, 'r', encoding='utf-8') as f:
            original_code = f.read()
        
        # Apply optimizations
        optimized_code = original_code
        
        if optimization_type == "performance":
            # Add caching decorators
            optimized_code = self._add_caching(optimized_code)
            
            # Optimize loops
            optimized_code = self._optimize_loops(optimized_code)
        
        elif optimization_type == "memory":
            # Use generators instead of lists where possible
            optimized_code = self._use_generators(optimized_code)
        
        if optimized_code == original_code:
            return None  # No changes made
        
        change = await self.propose_change(
            evolution_type=EvolutionType.OPTIMIZATION,
            file_path=file_path,
            new_code=optimized_code,
            description=f"Optimize {file_path} for {optimization_type}",
            source="auto-optimizer",
            confidence=0.6,
        )
        
        return change
    
