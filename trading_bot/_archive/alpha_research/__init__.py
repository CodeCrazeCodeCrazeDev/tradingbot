"""
alpha_research package - Research-Driven Alpha Operating System (RDAOS)

This package implements an institutional-grade quantitative research engine
that continuously extracts tradable, deployable alpha from academic research
and converts it into robust, risk-controlled trading features and strategies.

CORE MODULES (RDAOS):
1. rdaos_core - Core types and data structures
2. research_ingestion - Continuous research ingestion from academic sources
3. hypothesis_extraction - Convert papers into testable hypotheses
4. feature_synthesis - Translate hypotheses into feature families
5. sandbox_testing - Automated out-of-sample and cross-regime testing
6. feature_ranking - Rank and prune features for production
7. regime_meta_model - Regime-aware signal combination
8. live_deployment - Gradual scaling and kill-switch deployment
9. alpha_death_clock - Decay tracking and auto-retirement
10. weakness_detection - Root cause analysis and feedback loop
11. rdaos_orchestrator - Master orchestrator coordinating all components

HARD LIMITS ENFORCED:
- No strategies with unrealistic assumptions
- No recommendations without cost/latency constraints
- No overfitted or non-robust signals
- No ignoring risk controls or capacity limits
"""

