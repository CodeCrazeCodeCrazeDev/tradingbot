@dataclass
class TrailingStop:
    """Trailing stop configuration."""
    initial_distance: float  # Initial distance from entry price
    distance_type: str = "percent"  # "percent", "points", "atr"
    activation_threshold: Optional[float] = None  # Price must move this far in profit before trailing
    step_size: Optional[float] = None  # Minimum step size for stop adjustment
    acceleration_factor: Optional[float] = None  # For accelerating trailing stops
    max_distance: Optional[float] = None  # Maximum trailing distance
    
    def calculate_stop_level(self, 
                           entry_price: float, 
                           current_price: float, 
                           direction: str,
                           atr: Optional[float] = None) -> float:
        """
        Calculate current stop level.
        
        Args:
            entry_price: Entry price
            current_price: Current price
            direction: Trade direction ("long" or "short")
            atr: Optional ATR value for ATR-based stops
            
        Returns:
            Current stop level
        """
        # Calculate initial stop distance
        if self.distance_type == "percent":
            distance = entry_price * (self.initial_distance / 100)
        elif self.distance_type == "points":
            distance = self.initial_distance
        elif self.distance_type == "atr":
            if atr is None:
                raise ValueError("ATR value required for ATR-based trailing stop")
            distance = atr * self.initial_distance
        else:
            raise ValueError(f"Unknown distance type: {self.distance_type}")
        
        # Calculate initial stop level
        if direction == "long":
            initial_stop = entry_price - distance
            
            # Check if trailing is activated
            if self.activation_threshold is not None:
                activation_price = entry_price * (1 + self.activation_threshold / 100) if self.distance_type == "percent" else entry_price + self.activation_threshold
                if current_price < activation_price:
                    return initial_stop  # Not activated yet
            
            # Calculate trailing stop
            trailing_stop = current_price - distance
            
            # Apply step size if specified
            if self.step_size is not None and trailing_stop > initial_stop:
                steps = int((trailing_stop - initial_stop) / self.step_size)
                trailing_stop = initial_stop + steps * self.step_size
            
            # Apply maximum distance if specified
            if self.max_distance is not None:
                max_stop = entry_price + self.max_distance
                trailing_stop = min(trailing_stop, max_stop)
            
            # Never move stop lower
            return max(trailing_stop, initial_stop)
        else:
            # Short position
            initial_stop = entry_price + distance
            
            # Check if trailing is activated
            if self.activation_threshold is not None:
                activation_price = entry_price * (1 - self.activation_threshold / 100) if self.distance_type == "percent" else entry_price - self.activation_threshold
                if current_price > activation_price:
                    return initial_stop  # Not activated yet
            
            # Calculate trailing stop
            trailing_stop = current_price + distance
            
            # Apply step size if specified
            if self.step_size is not None and trailing_stop < initial_stop:
                steps = int((initial_stop - trailing_stop) / self.step_size)
                trailing_stop = initial_stop - steps * self.step_size
            
            # Apply maximum distance if specified
            if self.max_distance is not None:
                max_stop = entry_price - self.max_distance
                trailing_stop = max(trailing_stop, max_stop)
            
            # Never move stop higher
            return min(trailing_stop, initial_stop)


@dataclass
class TakeProfit:
    """Take profit configuration."""
    target: float  # Target price or percentage
    target_type: str = "percent"  # "percent", "points", "risk_reward"
    size_percentage: float = 100.0  # Percentage of position to exit
    
    def calculate_target_level(self, 
                             entry_price: float, 
                             direction: str,
                             stop_loss: Optional[float] = None) -> float:
        """
        Calculate take profit target level.
        
        Args:
            entry_price: Entry price
            direction: Trade direction ("long" or "short")
            stop_loss: Optional stop loss level for risk-reward based targets
            
        Returns:
            Take profit target level
        """
        if self.target_type == "percent":
            if direction == "long":
                return entry_price * (1 + self.target / 100)
            else:
                return entry_price * (1 - self.target / 100)
        elif self.target_type == "points":
            if direction == "long":
                return entry_price + self.target
            else:
                return entry_price - self.target
        elif self.target_type == "risk_reward":
            if stop_loss is None:
                raise ValueError("Stop loss required for risk-reward based take profit")
                
            # Calculate risk
            if direction == "long":
                risk = entry_price - stop_loss
                return entry_price + (risk * self.target)
            else:
                risk = stop_loss - entry_price
                return entry_price - (risk * self.target)
        else:
            raise ValueError(f"Unknown target type: {self.target_type}")


@dataclass
class BreakEven:
    """Break even stop configuration."""
    activation_threshold: float  # Price must move this far in profit before moving to breakeven
    threshold_type: str = "percent"  # "percent", "points", "risk_percent"
    buffer: float = 0.0  # Buffer to add to breakeven level
    buffer_type: str = "percent"  # "percent", "points"
    
    def is_activated(self, 
                   entry_price: float, 
                   current_price: float, 
                   direction: str,
                   stop_loss: Optional[float] = None) -> bool:
        """
        Check if breakeven stop should be activated.
        
        Args:
            entry_price: Entry price
            current_price: Current price
            direction: Trade direction ("long" or "short")
            stop_loss: Optional stop loss level for risk-percent based activation
            
        Returns:
            True if breakeven stop should be activated, False otherwise
        """
        if self.threshold_type == "percent":
            if direction == "long":
                activation_price = entry_price * (1 + self.activation_threshold / 100)
                return current_price >= activation_price
            else:
                activation_price = entry_price * (1 - self.activation_threshold / 100)
                return current_price <= activation_price
        elif self.threshold_type == "points":
            if direction == "long":
                activation_price = entry_price + self.activation_threshold
                return current_price >= activation_price
            else:
                activation_price = entry_price - self.activation_threshold
                return current_price <= activation_price
        elif self.threshold_type == "risk_percent":
            if stop_loss is None:
                raise ValueError("Stop loss required for risk-percent based breakeven")
                
            # Calculate risk
            if direction == "long":
                risk = entry_price - stop_loss
                activation_price = entry_price + (risk * self.activation_threshold)
                return current_price >= activation_price
            else:
                risk = stop_loss - entry_price
                activation_price = entry_price - (risk * self.activation_threshold)
                return current_price <= activation_price
        else:
            raise ValueError(f"Unknown threshold type: {self.threshold_type}")
    
    def calculate_breakeven_level(self, entry_price: float) -> float:
        """
        Calculate breakeven stop level with buffer.
        
        Args:
            entry_price: Entry price
            
        Returns:
            Breakeven stop level with buffer
        """
        if self.buffer_type == "percent":
            buffer_amount = entry_price * (self.buffer / 100)
        elif self.buffer_type == "points":
            buffer_amount = self.buffer
        else:
            raise ValueError(f"Unknown buffer type: {self.buffer_type}")
            
        return entry_price + buffer_amount


class ExitStrategy:
    """
    Base class for exit strategies that determine when and how to exit trades.
    """
    
    def __init__(self, config: Optional[Dict[str, Any]] = None):
        """
        Initialize exit strategy.
        
        Args:
            config: Optional configuration dictionary
        """
        self.config = config or {}
        self._init_default_config()
        
        logger.info(f"{self.__class__.__name__} initialized")
    
    def _init_default_config(self):
        """Initialize default configuration if not provided."""
        defaults = {
            "default_stop_loss_percent": 1.0,  # 1% default stop loss
            "default_take_profit_percent": 2.0,  # 2% default take profit
            "default_risk_reward_ratio": 2.0,  # 2:1 risk-reward ratio
            "enable_trailing_stop": True,  # Enable trailing stop
            "trailing_stop_activation_percent": 1.0,  # 1% profit before trailing
            "trailing_stop_distance_percent": 0.5,  # 0.5% trailing distance
            "enable_breakeven_stop": True,  # Enable breakeven stop
            "breakeven_activation_percent": 1.0,  # 1% profit before breakeven
            "breakeven_buffer_percent": 0.1,  # 0.1% buffer above entry
        }
        
        for key, value in defaults.items():
            if key not in self.config:
                self.config[key] = value
    
    def calculate_exit_levels(self, 
                            entry_price: float, 
                            direction: str, 
                            risk_percent: Optional[float] = None) -> Dict[str, float]:
        """
        Calculate exit levels for a trade.
        
        Args:
            entry_price: Entry price
            direction: Trade direction ("long" or "short")
            risk_percent: Optional risk percentage
            
        Returns:
            Dictionary of exit levels
        """
        # Use default stop loss if risk percent not provided
        stop_loss_percent = risk_percent or self.config["default_stop_loss_percent"]
        
        # Calculate stop loss level
        if direction == "long":
            stop_loss = entry_price * (1 - stop_loss_percent / 100)
        else:
            stop_loss = entry_price * (1 + stop_loss_percent / 100)
        
        # Calculate take profit based on risk-reward ratio
        risk_reward = self.config["default_risk_reward_ratio"]
        
        if direction == "long":
            risk = entry_price - stop_loss
            take_profit = entry_price + (risk * risk_reward)
        else:
            risk = stop_loss - entry_price
            take_profit = entry_price - (risk * risk_reward)
        
        # Calculate breakeven level
        breakeven = entry_price
        if self.config["enable_breakeven_stop"]:
            buffer_percent = self.config["breakeven_buffer_percent"]
            if direction == "long":
                breakeven = entry_price * (1 + buffer_percent / 100)
            else:
                breakeven = entry_price * (1 - buffer_percent / 100)
        
        return {
            "stop_loss": stop_loss,
            "take_profit": take_profit,
            "breakeven": breakeven
        }
    
    def check_exit_conditions(self, 
                            entry_price: float, 
                            current_price: float, 
                            direction: str,
                            stop_loss: float,
                            take_profit: float,
                            trailing_stop: Optional[float] = None) -> Tuple[bool, Optional[ExitReason], Optional[float]]:
        """
        Check if exit conditions are met.
        
        Args:
            entry_price: Entry price
            current_price: Current price
            direction: Trade direction ("long" or "short")
            stop_loss: Stop loss level
            take_profit: Take profit level
            trailing_stop: Optional trailing stop level
            
        Returns:
            Tuple of (should_exit, exit_reason, exit_price)
        """
        if direction == "long":
            # Check stop loss
            if current_price <= stop_loss:
                return True, ExitReason.STOP_LOSS_HIT, stop_loss
            
            # Check trailing stop
            if trailing_stop is not None and current_price <= trailing_stop:
                return True, ExitReason.TRAILING_STOP_HIT, trailing_stop
            
            # Check take profit
            if current_price >= take_profit:
                return True, ExitReason.TAKE_PROFIT_HIT, take_profit
        else:
            # Check stop loss
            if current_price >= stop_loss:
                return True, ExitReason.STOP_LOSS_HIT, stop_loss
            
            # Check trailing stop
            if trailing_stop is not None and current_price >= trailing_stop:
                return True, ExitReason.TRAILING_STOP_HIT, trailing_stop
            
            # Check take profit
            if current_price <= take_profit:
                return True, ExitReason.TAKE_PROFIT_HIT, take_profit
        
        # No exit conditions met
        return False, None, None
    
    def create_exit_signal(self, 
                         symbol: str, 
                         exit_reason: ExitReason, 
                         exit_price: float,
                         position_id: Optional[str] = None,
                         direction: Optional[str] = None) -> ExitSignal:
        """
        Create an exit signal.
        
        Args:
            symbol: Symbol
            exit_reason: Reason for exit
            exit_price: Exit price
            position_id: Optional position ID
            direction: Optional trade direction
            
        Returns:
            Exit signal
        """
        # Map exit reason to exit type
        exit_type_map = {
            ExitReason.STOP_LOSS_HIT: ExitType.FIXED_STOP,
            ExitReason.TAKE_PROFIT_HIT: ExitType.FIXED_TAKE_PROFIT,
            ExitReason.TRAILING_STOP_HIT: ExitType.TRAILING_STOP,
            ExitReason.TIME_EXIT: ExitType.TIME_BASED_EXIT,
            ExitReason.TREND_CHANGE: ExitType.TREND_REVERSAL,
            ExitReason.INDICATOR_SIGNAL: ExitType.INDICATOR_BASED,
            ExitReason.VOLATILITY_EVENT: ExitType.VOLATILITY_EXPANSION,
            ExitReason.MARKET_STRUCTURE_CHANGE: ExitType.MARKET_STRUCTURE,
            ExitReason.RISK_MANAGEMENT: ExitType.CUSTOM,
            ExitReason.MANUAL_EXIT: ExitType.CUSTOM,
            ExitReason.TARGET_REACHED: ExitType.FIXED_TAKE_PROFIT,
            ExitReason.OPPORTUNITY_COST: ExitType.CUSTOM,
            ExitReason.CUSTOM: ExitType.CUSTOM
        }
        
        exit_type = exit_type_map.get(exit_reason, ExitType.CUSTOM)
        
        # Create exit signal
        signal_id = f"exit_{uuid.uuid4().hex[:8]}"
        
        signal = ExitSignal(
            id=signal_id,
            symbol=symbol,
            timestamp=datetime.now(),
            exit_type=exit_type,
            exit_reason=exit_reason,
            price=exit_price,
            position_id=position_id,
            direction=direction,
            size_percentage=100.0,  # Full exit by default
            confidence=1.0,  # Full confidence by default
            notes=f"Exit triggered by {exit_reason.value}"
        )
        
        return signal
    
    def update_trailing_stop(self, 
                           entry_price: float, 
                           current_price: float, 
                           direction: str,
                           trailing_config: Optional[TrailingStop] = None,
                           current_stop: Optional[float] = None,
                           atr: Optional[float] = None) -> float:
        """
        Update trailing stop level.
        
        Args:
            entry_price: Entry price
            current_price: Current price
            direction: Trade direction ("long" or "short")
            trailing_config: Optional trailing stop configuration
            current_stop: Current stop level
            atr: Optional ATR value
            
        Returns:
            Updated trailing stop level
        """
        # Use default configuration if not provided
        if trailing_config is None:
            trailing_config = TrailingStop(
                initial_distance=self.config["trailing_stop_distance_percent"],
                distance_type="percent",
                activation_threshold=self.config["trailing_stop_activation_percent"]
            )
        
        # Calculate new stop level
        new_stop = trailing_config.calculate_stop_level(
            entry_price=entry_price,
            current_price=current_price,
            direction=direction,
            atr=atr
        )
        
        # Only move stop in favorable direction
        if current_stop is not None:
            if direction == "long":
                new_stop = max(new_stop, current_stop)
            else:
                new_stop = min(new_stop, current_stop)
        
        return new_stop
    
    def check_breakeven_stop(self, 
                           entry_price: float, 
                           current_price: float, 
                           direction: str,
                           breakeven_config: Optional[BreakEven] = None,
                           stop_loss: Optional[float] = None) -> Tuple[bool, Optional[float]]:
        """
        Check if breakeven stop should be activated.
        
        Args:
            entry_price: Entry price
            current_price: Current price
            direction: Trade direction ("long" or "short")
            breakeven_config: Optional breakeven configuration
            stop_loss: Optional stop loss level
            
        Returns:
            Tuple of (should_activate, breakeven_level)
        """
        # Use default configuration if not provided
        if breakeven_config is None:
            breakeven_config = BreakEven(
                activation_threshold=self.config["breakeven_activation_percent"],
                threshold_type="percent",
                buffer=self.config["breakeven_buffer_percent"],
                buffer_type="percent"
            )
        
        # Check if breakeven should be activated
        if breakeven_config.is_activated(
            entry_price=entry_price,
            current_price=current_price,
            direction=direction,
            stop_loss=stop_loss
        ):
            # Calculate breakeven level
            breakeven_level = breakeven_config.calculate_breakeven_level(entry_price)
            return True, breakeven_level
        
        return False, None
