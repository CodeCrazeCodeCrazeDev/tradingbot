class TaskType(Enum):
    """Types of code mending tasks"""
    BUG_FIX = "bug_fix"
    CODE_REFACTOR = "code_refactor"
    OPTIMIZATION = "optimization"
    TEST_GENERATION = "test_generation"
    DOCUMENTATION = "documentation"
    SECURITY_FIX = "security_fix"
    STYLE_FIX = "style_fix"
    IMPORT_FIX = "import_fix"
    GUARD_CLAUSE_FIX = "guard_clause_fix"
    DUPLICATE_REMOVAL = "duplicate_removal"


@dataclass
class CodeMenderConfig:
    """Configuration for Qwen 3 8B CodeMender"""
    # Model settings
    backend: InferenceBackend = InferenceBackend.OLLAMA
    endpoint: str = "http://localhost:11434/api/generate"
    model_name: str = "qwen2.5-coder:1.5b"
    temperature: float = 0.2
    max_tokens: int = 2048
    timeout: int = 120
    retry_attempts: int = 1

    # Context management
    max_context_length: int = 32768
    context_window_overlap: int = 512

    # Safety settings
    safety_level: SafetyLevel = SafetyLevel.SUPERVISED
    require_approval: bool = True
    sandbox_mode: bool = True
    auto_commit_safe_changes: bool = False
    max_changes_per_hour: int = 20
    max_changes_per_day: int = 100

    # Codebase
    root_path: str = ""
    backup_dir: Optional[str] = None


@dataclass
class CodeChange:
    """Represents a code change generated by CodeMender"""
    file_path: str
    original_code: str
    modified_code: str
    explanation: str
    diff: str = ""
    safety_score: float = 0.0
    requires_review: bool = True
    task_type: TaskType = TaskType.BUG_FIX


@dataclass
class MendingTask:
    """Represents a code mending task"""
    task_id: str
    task_type: TaskType
    description: str
    target_files: List[str] = field(default_factory=list)
    context: Dict[str, Any] = field(default_factory=dict)
    priority: int = 5
    created_at: datetime = field(default_factory=datetime.now)
    status: str = "pending"


@dataclass
class MendingResult:
    """Result of a code mending operation"""
    task_id: str
    success: bool
    changes: List[CodeChange] = field(default_factory=list)
    errors: List[str] = field(default_factory=list)
    duration_seconds: float = 0.0
    tokens_used: int = 0


SYSTEM_PROMPT = """You are Qwen 3 0.6B CodeMender, an expert Python code repair and optimization AI.
You specialize in fixing trading bot codebases. Your capabilities:

1. **Bug Fixing**: Identify and fix syntax errors, logic bugs, broken guard clauses
2. **Code Optimization**: Improve performance, reduce complexity, clean up dead code
3. **Security**: Fix vulnerabilities, enforce safety guardrails
4. **Refactoring**: Remove duplicates, fix imports, improve structure
5. **Testing**: Generate unit tests for critical paths

Rules:
- Always output valid Python code
- Never modify risk management logic without explicit approval
- Preserve existing code style and comments
- Explain every change you make
- If unsure, ask rather than guess
- Never hardcode credentials or API keys
"""


class QwenCodeMender:
    """
    Main CodeMender engine powered by Qwen 3 8B.

    Provides AI-driven code analysis, repair, and optimization
    for the trading bot codebase.
    """

    def __init__(self, config: Optional[CodeMenderConfig] = None):
        self.config = config or CodeMenderConfig()

        if not self.config.root_path:
            self.config.root_path = str(Path.cwd())

        self.client = QwenInferenceClient(
            endpoint=self.config.endpoint,
            model_name=self.config.model_name,
            backend=self.config.backend,
            temperature=self.config.temperature,
            max_tokens=self.config.max_tokens,
            timeout=self.config.timeout,
            retry_attempts=self.config.retry_attempts,
            max_context_length=self.config.max_context_length,
        )

        self.analyzer = CodeAnalyzer(self.config.root_path)

        self.guardrails = SafetyGuardrails(
            root_path=self.config.root_path,
            safety_level=self.config.safety_level,
            max_changes_per_hour=self.config.max_changes_per_hour,
            max_changes_per_day=self.config.max_changes_per_day,
            backup_dir=self.config.backup_dir,
        )

        self._task_history: List[MendingResult] = []
        logger.info(f"QwenCodeMender initialized (model={self.config.model_name}, safety={self.config.safety_level.value})")

    async def health_check(self) -> Dict[str, Any]:
        """Check if the Qwen inference server is reachable"""
        server_ok = await self.client.health_check()
        return {
            "server_reachable": server_ok,
            "model": self.config.model_name,
            "backend": self.config.backend.value,
            "endpoint": self.config.endpoint,
            "safety_level": self.config.safety_level.value,
        }

    def scan_codebase(self) -> AnalysisReport:
        """Run a full codebase analysis"""
        return self.analyzer.full_scan()

    async def fix_file(self, file_path: str, issue_description: str = "") -> MendingResult:
        """
        Analyze and fix issues in a single file.

        Args:
            file_path: Path to the file to fix
            issue_description: Optional description of the issue

        Returns:
            MendingResult with the changes made
        """
        import time as _time
        start = _time.monotonic()
        task_id = f"fix_{datetime.now().strftime('%Y%m%d_%H%M%S')}"

        result = MendingResult(task_id=task_id, success=False)

        try:
            if not os.path.exists(file_path):
                result.errors.append(f"File not found: {file_path}")
                return result

            with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
                original = f.read()

            # Build prompt
            prompt = self._build_fix_prompt(file_path, original, issue_description)

            # Get AI response
            response = await self.client.generate(
                prompt=prompt,
                system_prompt=SYSTEM_PROMPT,
            )
            result.tokens_used = response.tokens_used

            # Parse the response for code changes
            modified = self._extract_code_from_response(response.text, original)

            if modified and modified != original:
                # Validate through guardrails
                validation = self.guardrails.validate_change(file_path, original, modified)

                change = CodeChange(
                    file_path=file_path,
                    original_code=original,
                    modified_code=modified,
                    explanation=self._extract_explanation(response.text),
                    safety_score=1.0 if validation["approved"] else 0.5,
                    requires_review=validation.get("needs_human_approval", True),
                )
                result.changes.append(change)

                if validation["approved"] and not change.requires_review:
                    with open(file_path, 'w', encoding='utf-8') as f:
                        f.write(modified)
                    logger.info(f"Auto-applied fix to {file_path}")
                    result.success = True
                elif validation.get("needs_human_approval"):
                    logger.info(f"Fix for {file_path} requires human approval (risk: {validation['risk_level']})")
                    result.success = True
                else:
                    result.errors.append(validation.get("reason", "Change rejected"))
            else:
                result.success = True  # No changes needed

        except Exception as e:
            result.errors.append(str(e))
            logger.error(f"Error fixing {file_path}: {e}")

        result.duration_seconds = _time.monotonic() - start
        self._task_history.append(result)
        return result

    async def generate_fix(
        self, code: str, issue: str, context: str = ""
    ) -> Optional[str]:
        """
        Generate a fix for a code snippet without applying it.

        Args:
            code: The code to fix
            issue: Description of the issue
            context: Additional context

        Returns:
            Fixed code string or None
        """
        prompt = f"""Fix the following issue in this Python code:

Issue: {issue}

```python
{code}
```

{f'Context: {context}' if context else ''}

Return ONLY the fixed code inside a ```python code block. No explanations outside the code block."""

        response = await self.client.generate(
            prompt=prompt,
            system_prompt=SYSTEM_PROMPT,
        )

        return self._extract_code_from_response(response.text, code)

    async def explain_code(self, code: str) -> str:
        """Get an explanation of what a code snippet does"""
        prompt = f"""Explain what this Python code does. Be concise and focus on the logic:

```python
{code}
```"""

        response = await self.client.generate(
            prompt=prompt,
            system_prompt=SYSTEM_PROMPT,
            temperature=0.3,
        )
        return response.text

    async def generate_tests(self, file_path: str) -> Optional[str]:
        """Generate unit tests for a file"""
        if not os.path.exists(file_path):
            return None

        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            code = f.read()

        prompt = f"""Generate comprehensive pytest unit tests for this Python module:

File: {os.path.basename(file_path)}

```python
{code[:self.config.max_context_length // 2]}
```

Generate tests that cover:
1. Normal operation
2. Edge cases
3. Error handling
4. Boundary conditions

Return ONLY the test code inside a ```python code block."""

        response = await self.client.generate(
            prompt=prompt,
            system_prompt=SYSTEM_PROMPT,
            max_tokens=self.config.max_tokens,
        )

        return self._extract_code_from_response(response.text, "")

    def _build_fix_prompt(self, file_path: str, code: str, issue: str) -> str:
        """Build a prompt for fixing a file"""
        # Truncate if too long
        max_code = self.config.max_context_length // 2
        if len(code) > max_code:
            code = code[:max_code] + "\n# ... (truncated)"

        parts = [
            f"Analyze and fix issues in this Python file: {os.path.basename(file_path)}",
        ]
        if issue:
            parts.append(f"\nSpecific issue: {issue}")

        parts.append(f"\n```python\n{code}\n```")
        parts.append(
            "\nReturn the COMPLETE fixed file inside a ```python code block. "
            "After the code block, explain what you changed and why."
        )
        return "\n".join(parts)

    def _extract_code_from_response(self, response: str, fallback: str) -> str:
        """Extract Python code from an AI response"""
        # Look for ```python ... ``` blocks
        import re
        pattern = r'```python\s*\n(.*?)```'
        matches = re.findall(pattern, response, re.DOTALL)

        if matches:
            # Return the longest match (likely the full file)
            code = max(matches, key=len).strip()
            # Validate syntax
            if self.guardrails.validate_syntax(code):
                return code
            logger.warning("Extracted code has syntax errors, returning fallback")

        return fallback

    def _extract_explanation(self, response: str) -> str:
        """Extract explanation text from an AI response"""
        import re
        # Get text after the last code block
        parts = re.split(r'```', response)
        if len(parts) >= 3:
            return parts[-1].strip()
        return response[:500]

    def get_stats(self) -> Dict[str, Any]:
        """Get CodeMender statistics"""
        total_changes = sum(len(r.changes) for r in self._task_history)
        total_tokens = sum(r.tokens_used for r in self._task_history)
        successful = sum(1 for r in self._task_history if r.success)

        return {
            "model": self.config.model_name,
            "backend": self.config.backend.value,
            "safety_level": self.config.safety_level.value,
            "total_tasks": len(self._task_history),
            "successful_tasks": successful,
            "total_changes": total_changes,
            "total_tokens": total_tokens,
            "inference_stats": self.client.get_stats(),
            "guardrail_stats": self.guardrails.get_stats(),
        }

    async def close(self):
        """Clean up resources"""
        await self.client.close()

    async def __aenter__(self):
        return self

    async def __aexit__(self, *args):
        await self.close()


def create_codemender(
    root_path: str = "",
    model_name: str = "qwen2.5-coder:1.5b",
    endpoint: str = "http://localhost:11434/api/generate",
    safety_level: SafetyLevel = SafetyLevel.SUPERVISED,
) -> QwenCodeMender:
    """Factory function to create a QwenCodeMender instance"""
    config = CodeMenderConfig(
        root_path=root_path or str(Path.cwd()),
        model_name=model_name,
        endpoint=endpoint,
        safety_level=safety_level,
    )
    return QwenCodeMender(config)


async def quick_fix(file_path: str, issue: str = "") -> MendingResult:
    """Quick one-shot fix for a file"""
    mender = create_codemender(root_path=str(Path(file_path).parent.parent))
    try:
        return await mender.fix_file(file_path, issue)
    finally:
        await mender.close()


async def quick_validate(root_path: str = "") -> AnalysisReport:
    """Quick codebase validation"""
    mender = create_codemender(root_path=root_path or str(Path.cwd()))
    try:
        return mender.scan_codebase()
    finally:
        await mender.close()
