# Hivemind Integration - Makes RadarAI controlled by Hivemind
try:
    from ..hivemind import HivemindAgentController
    
    class RadarAIHivemindAdapter:
        """
        Adapter that connects RadarAI systems to the Hivemind controller.
        
        This ensures all RadarAI agents and orchestrators are controlled
        by the central hivemind intelligence.
        """
        
        def __init__(self, hivemind_controller=None):
            self.hivemind = hivemind_controller
            self.radar_orchestrator = None
            self.hivemind_controller = None
            self._initialized = False
        
        async def initialize(self, config=None):
            """Initialize RadarAI under hivemind control."""
            config = config or {}
            
            # Initialize RadarAI orchestrator
            self.radar_orchestrator = RadarAIOrchestrator(
                mode=OperationMode.SEMI_AUTONOMOUS,
                enable_superintelligence=config.get('enable_superintelligence', False)
            )
            
            # Initialize hivemind controller for agents
            self.hivemind_controller = HivemindController(config=config)
            
            self._initialized = True
            return self
        
        def get_status(self):
            """Get status of RadarAI systems."""
            return {
                'initialized': self._initialized,
                'orchestrator_active': self.radar_orchestrator is not None,
                'hivemind_controller_active': self.hivemind_controller is not None,
            }
    
    # Auto-register with hivemind if available
    _hivemind_adapter = None
    
    def get_hivemind_adapter(config=None):
        """Get or create the hivemind adapter for RadarAI."""
        global _hivemind_adapter
        if _hivemind_adapter is None:
            _hivemind_adapter = RadarAIHivemindAdapter()
        return _hivemind_adapter

except ImportError:
    RadarAIHivemindAdapter = None


