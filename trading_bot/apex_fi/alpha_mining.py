class AlphaExpressionType(str, Enum):
    """Types of alpha expressions."""
    MOMENTUM = "momentum"
    MEAN_REVERSION = "mean_reversion"
    VOLATILITY = "volatility"
    CORRELATION = "correlation"
    MICROSTRUCTURE = "microstructure"
    FUNDAMENTAL = "fundamental"
    ALTERNATIVE_DATA = "alternative_data"
    CROSS_ASSET = "cross_asset"
    REGIME_DEPENDENT = "regime_dependent"


class FactorStatus(str, Enum):
    """Factor lifecycle status."""
    CANDIDATE = "candidate"           # Under evaluation
    VALIDATED = "validated"           # Passed statistical tests
    PRODUCTION = "production"         # Deployed in live trading
    DECAYING = "decaying"             # Performance degrading
    RETIRED = "retired"               # No longer effective


@dataclass
class AlphaCandidate:
    """
    Candidate alpha signal for evaluation.
    
    Generated by genetic search, LLM hypothesis generation,
    or unsupervised pattern mining.
    """
    
    candidate_id: str
    expression: str
    expression_type: AlphaExpressionType
    generated_at: datetime = field(default_factory=datetime.now)
    generated_by: str = "unknown"
    
    # Statistical properties
    information_coefficient: Optional[float] = None
    sharpe_ratio: Optional[float] = None
    decay_rate: Optional[float] = None
    t_statistic: Optional[float] = None
    
    # Validation status
    status: FactorStatus = FactorStatus.CANDIDATE
    validation_samples: int = 0
    
    # Metadata
    asset_classes: List[str] = field(default_factory=list)
    market_regimes: List[str] = field(default_factory=list)
    capacity_estimate: Optional[float] = None
    correlation_profile: Dict[str, float] = field(default_factory=dict)
    
    def to_dict(self) -> Dict[str, Any]:
        """Serialize to dictionary."""
        return {
            'candidate_id': self.candidate_id,
            'expression': self.expression,
            'expression_type': self.expression_type.value,
            'generated_at': self.generated_at.isoformat(),
            'generated_by': self.generated_by,
            'information_coefficient': self.information_coefficient,
            'sharpe_ratio': self.sharpe_ratio,
            'decay_rate': self.decay_rate,
            't_statistic': self.t_statistic,
            'status': self.status.value,
            'validation_samples': self.validation_samples,
            'asset_classes': self.asset_classes,
            'market_regimes': self.market_regimes,
        }


@dataclass
class FactorMetadata:
    """
    Metadata for a production factor.
    
    Tracks performance, decay, regimes, and capacity.
    """
    
    factor_id: str
    expression: str
    discovery_timestamp: datetime
    
    # Performance metrics
    current_ic: float = 0.0
    current_sharpe: float = 0.0
    peak_sharpe: float = 0.0
    decay_rate: float = 0.0
    
    # Regime effectiveness
    regime_performance: Dict[str, float] = field(default_factory=dict)
    
    # Capacity and correlation
    capacity_estimate: float = 0.0
    correlation_profile: Dict[str, float] = field(default_factory=dict)
    
    # Lifecycle tracking
    status: FactorStatus = FactorStatus.PRODUCTION
    last_updated: datetime = field(default_factory=datetime.now)
    retirement_reason: Optional[str] = None
    
    def update_performance(self, ic: float, sharpe: float) -> None:
        """Update performance metrics."""
        self.current_ic = ic
        self.current_sharpe = sharpe
        
        if sharpe > self.peak_sharpe:
            self.peak_sharpe = sharpe
        
        # Calculate decay
        if self.peak_sharpe > 0:
            self.decay_rate = (self.peak_sharpe - sharpe) / self.peak_sharpe
        
        self.last_updated = datetime.now()
    
    def should_retire(self, decay_threshold: float = 0.5) -> bool:
        """Check if factor should be retired."""
        return self.decay_rate > decay_threshold
    
    def to_dict(self) -> Dict[str, Any]:
        """Serialize to dictionary."""
        return {
            'factor_id': self.factor_id,
            'expression': self.expression,
            'discovery_timestamp': self.discovery_timestamp.isoformat(),
            'current_ic': self.current_ic,
            'current_sharpe': self.current_sharpe,
            'peak_sharpe': self.peak_sharpe,
            'decay_rate': self.decay_rate,
            'status': self.status.value,
            'last_updated': self.last_updated.isoformat(),
        }


class GeneticAlphaSearch:
    """
    Genetic algorithm for alpha signal discovery.
    
    Breeds, mutates, and selects signal expressions across a grammar
    of financial operations. Evaluates 10M+ candidates per day.
    """
    
    def __init__(
        self,
        population_size: int = 1000,
        mutation_rate: float = 0.1,
        crossover_rate: float = 0.7
    ):
        self.population_size = population_size
        self.mutation_rate = mutation_rate
        self.crossover_rate = crossover_rate
        
        self._population: List[AlphaCandidate] = []
        self._generation = 0
        self._total_evaluated = 0
        
        # Financial operation grammar
        self._operations = [
            'rank', 'zscore', 'decay_linear', 'ts_mean', 'ts_std',
            'ts_rank', 'delta', 'correlation', 'covariance',
            'ts_min', 'ts_max', 'ts_argmax', 'ts_argmin'
        ]
        
        self._data_fields = [
            'close', 'volume', 'high', 'low', 'vwap',
            'returns', 'log_returns', 'volatility'
        ]
        
        logger.info(
            f"Genetic Alpha Search initialized - "
            f"Population: {population_size}, "
            f"Mutation: {mutation_rate}, "
            f"Crossover: {crossover_rate}"
        )
    
    def initialize_population(self) -> None:
        """Initialize random population."""
        self._population = []
        
        for i in range(self.population_size):
            expression = self._generate_random_expression()
            candidate = AlphaCandidate(
                candidate_id=f"gen0_ind{i}",
                expression=expression,
                expression_type=self._classify_expression(expression),
                generated_by="genetic_init"
            )
            self._population.append(candidate)
        
        logger.info(f"Initialized population with {self.population_size} candidates")
    
    def _generate_random_expression(self, depth: int = 0, max_depth: int = 3) -> str:
        """Generate random alpha expression."""
        if depth >= max_depth:
            # Terminal: return a data field
            return random.choice(self._data_fields)
        
        # Choose operation
        op = random.choice(self._operations)
        
        # Generate operands based on operation
        if op in ['rank', 'zscore', 'delta']:
            # Unary operations
            operand = self._generate_random_expression(depth + 1, max_depth)
            return f"{op}({operand})"
        
        elif op in ['ts_mean', 'ts_std', 'ts_rank', 'decay_linear']:
            # Time series operations with window
            operand = self._generate_random_expression(depth + 1, max_depth)
            window = random.choice([5, 10, 20, 60])
            return f"{op}({operand}, {window})"
        
        elif op in ['correlation', 'covariance']:
            # Binary operations
            operand1 = self._generate_random_expression(depth + 1, max_depth)
            operand2 = self._generate_random_expression(depth + 1, max_depth)
            window = random.choice([20, 60, 120])
            return f"{op}({operand1}, {operand2}, {window})"
        
        else:
            # Default: unary
            operand = self._generate_random_expression(depth + 1, max_depth)
            return f"{op}({operand})"
    
    def _classify_expression(self, expression: str) -> AlphaExpressionType:
        """Classify expression type based on operations used."""
        if 'correlation' in expression or 'covariance' in expression:
            return AlphaExpressionType.CORRELATION
        elif 'ts_std' in expression or 'volatility' in expression:
            return AlphaExpressionType.VOLATILITY
        elif 'delta' in expression or 'returns' in expression:
            return AlphaExpressionType.MOMENTUM
        elif 'ts_mean' in expression or 'decay_linear' in expression:
            return AlphaExpressionType.MEAN_REVERSION
        else:
            return AlphaExpressionType.MOMENTUM
    
    def evolve_generation(
        self,
        fitness_function: Callable[[AlphaCandidate], float]
    ) -> List[AlphaCandidate]:
        """
        Evolve one generation.
        
        Args:
            fitness_function: Function that evaluates candidate fitness
            
        Returns:
            New generation of candidates
        """
        # Evaluate fitness
        fitness_scores = []
        for candidate in self._population:
            score = fitness_function(candidate)
            fitness_scores.append((candidate, score))
        
        # Sort by fitness
        fitness_scores.sort(key=lambda x: x[1], reverse=True)
        
        # Select top performers for breeding
        elite_size = int(0.1 * self.population_size)
        elite = [c for c, _ in fitness_scores[:elite_size]]
        
        # Generate new population
        new_population = elite.copy()  # Elitism
        
        while len(new_population) < self.population_size:
            # Selection
            parent1 = self._tournament_selection(fitness_scores)
            parent2 = self._tournament_selection(fitness_scores)
            
            # Crossover
            if random.random() < self.crossover_rate:
                child_expr = self._crossover(parent1.expression, parent2.expression)
            else:
                child_expr = parent1.expression
            
            # Mutation
            if random.random() < self.mutation_rate:
                child_expr = self._mutate(child_expr)
            
            # Create child
            child = AlphaCandidate(
                candidate_id=f"gen{self._generation + 1}_ind{len(new_population)}",
                expression=child_expr,
                expression_type=self._classify_expression(child_expr),
                generated_by=f"genetic_gen{self._generation + 1}"
            )
            new_population.append(child)
        
        self._population = new_population
        self._generation += 1
        self._total_evaluated += len(new_population)
        
        logger.info(
            f"Generation {self._generation} complete - "
            f"Best fitness: {fitness_scores[0][1]:.4f}, "
            f"Total evaluated: {self._total_evaluated}"
        )
        
        return new_population
    
    def _tournament_selection(
        self,
        fitness_scores: List[tuple],
        tournament_size: int = 5
    ) -> AlphaCandidate:
        """Tournament selection."""
        tournament = random.sample(fitness_scores, tournament_size)
        winner = max(tournament, key=lambda x: x[1])
        return winner[0]
    
    def _crossover(self, expr1: str, expr2: str) -> str:
        """Crossover two expressions."""
        # Simple crossover: randomly choose parts from each parent
        if random.random() < 0.5:
            return expr1
        else:
            return expr2
    
    def _mutate(self, expression: str) -> str:
        """Mutate an expression."""
        # Simple mutation: regenerate a random expression
        return self._generate_random_expression()
    
    def get_best_candidates(self, n: int = 10) -> List[AlphaCandidate]:
        """Get top N candidates from current population."""
        # Sort by sharpe ratio
        sorted_pop = sorted(
            self._population,
            key=lambda c: c.sharpe_ratio if c.sharpe_ratio else -999,
            reverse=True
        )
        return sorted_pop[:n]
    
    def get_stats(self) -> Dict[str, Any]:
        """Get search statistics."""
        return {
            'generation': self._generation,
            'population_size': len(self._population),
            'total_evaluated': self._total_evaluated,
            'mutation_rate': self.mutation_rate,
            'crossover_rate': self.crossover_rate,
        }


class LivingFactorLibrary:
    """
    Living library of 50,000+ factors.
    
    Each factor is tagged with:
    - Market regimes where it exhibits statistical power
    - Current decay rate
    - Correlation profile
    - Capacity constraints
    - Discovery timestamp
    
    Factors are automatically retired when decay exceeds thresholds.
    """
    
    def __init__(self, decay_threshold: float = 0.5):
        self.decay_threshold = decay_threshold
        self._factors: Dict[str, FactorMetadata] = {}
        self._retired_factors: Dict[str, FactorMetadata] = {}
        
        logger.info(f"Living Factor Library initialized - Decay threshold: {decay_threshold}")
    
    def add_factor(self, factor: FactorMetadata) -> None:
        """Add factor to library."""
        self._factors[factor.factor_id] = factor
        logger.info(f"Added factor: {factor.factor_id}")
    
    def update_factor_performance(
        self,
        factor_id: str,
        ic: float,
        sharpe: float
    ) -> None:
        """Update factor performance metrics."""
        if factor_id not in self._factors:
            logger.warning(f"Factor {factor_id} not found")
            return
        
        factor = self._factors[factor_id]
        factor.update_performance(ic, sharpe)
        
        # Check for retirement
        if factor.should_retire(self.decay_threshold):
            self._retire_factor(factor_id, "decay_threshold_exceeded")
    
    def _retire_factor(self, factor_id: str, reason: str) -> None:
        """Retire a factor."""
        if factor_id not in self._factors:
            return
        
        factor = self._factors.pop(factor_id)
        factor.status = FactorStatus.RETIRED
        factor.retirement_reason = reason
        self._retired_factors[factor_id] = factor
        
        logger.info(f"Retired factor: {factor_id} - Reason: {reason}")
    
    def get_active_factors(self) -> List[FactorMetadata]:
        """Get all active factors."""
        return list(self._factors.values())
    
    def get_factors_by_regime(self, regime: str) -> List[FactorMetadata]:
        """Get factors effective in a specific regime."""
        return [
            f for f in self._factors.values()
            if regime in f.regime_performance and f.regime_performance[regime] > 0
        ]
    
    def get_decaying_factors(self) -> List[FactorMetadata]:
        """Get factors showing decay."""
        return [
            f for f in self._factors.values()
            if f.status == FactorStatus.DECAYING
        ]
    
    def get_stats(self) -> Dict[str, Any]:
        """Get library statistics."""
        return {
            'total_active_factors': len(self._factors),
            'total_retired_factors': len(self._retired_factors),
            'factors_by_status': {
                status.value: len([
                    f for f in self._factors.values()
                    if f.status == status
                ])
                for status in FactorStatus
            },
            'decay_threshold': self.decay_threshold,
        }


class AlphaMiningEngine:
    """
    Autonomous Signal Discovery & Alpha Mining Engine.
    
    Orchestrates:
    - Genetic alpha search
    - LLM hypothesis generation
    - Causal discovery
    - Factor library management
    - Cross-asset signal transfer
    """
    
    def __init__(self):
        self.genetic_search = GeneticAlphaSearch(population_size=1000)
        self.factor_library = LivingFactorLibrary(decay_threshold=0.5)
        
        self._running = False
        self._candidates_generated_today = 0
        self._candidates_validated_today = 0
        self._last_reset = datetime.now()
        
        logger.info("Alpha Mining Engine initialized - Two Sigma Bloodline")
    
    async def start(self) -> None:
        """Start alpha mining engine."""
        self._running = True
        logger.info("Alpha Mining Engine started")
        
        # Initialize genetic search
        self.genetic_search.initialize_population()
        
        # Start background tasks
        asyncio.create_task(self._continuous_discovery())
        asyncio.create_task(self._monitor_factor_decay())
        asyncio.create_task(self._daily_reset())
    
    async def stop(self) -> None:
        """Stop alpha mining engine."""
        self._running = False
        logger.info("Alpha Mining Engine stopped")
    
    async def _continuous_discovery(self) -> None:
        """Continuous alpha discovery loop."""
        while self._running:
            # Run genetic evolution
            def fitness_fn(candidate: AlphaCandidate) -> float:
                # Placeholder fitness function
                # In production, this would backtest the candidate
                return random.random()
            
            new_candidates = self.genetic_search.evolve_generation(fitness_fn)
            self._candidates_generated_today += len(new_candidates)
            
            # Evaluate top candidates
            top_candidates = self.genetic_search.get_best_candidates(n=10)
            for candidate in top_candidates:
                if candidate.sharpe_ratio and candidate.sharpe_ratio > 1.5:
                    # Promote to factor library
                    factor = FactorMetadata(
                        factor_id=candidate.candidate_id,
                        expression=candidate.expression,
                        discovery_timestamp=candidate.generated_at,
                        current_sharpe=candidate.sharpe_ratio,
                        peak_sharpe=candidate.sharpe_ratio,
                    )
                    self.factor_library.add_factor(factor)
                    self._candidates_validated_today += 1
            
            await asyncio.sleep(60)  # Run every minute
    
    async def _monitor_factor_decay(self) -> None:
        """Monitor factor decay and retire underperformers."""
        while self._running:
            decaying = self.factor_library.get_decaying_factors()
            
            if decaying:
                logger.warning(f"Detected {len(decaying)} decaying factors")
            
            await asyncio.sleep(3600)  # Check every hour
    
    async def _daily_reset(self) -> None:
        """Reset daily counters."""
        while self._running:
            now = datetime.now()
            if (now - self._last_reset).days >= 1:
                logger.info(
                    f"Daily stats - Generated: {self._candidates_generated_today}, "
                    f"Validated: {self._candidates_validated_today}"
                )
                self._candidates_generated_today = 0
                self._candidates_validated_today = 0
                self._last_reset = now
            
            await asyncio.sleep(3600)  # Check every hour
    
    def get_status(self) -> Dict[str, Any]:
        """Get alpha mining engine status."""
        return {
            'running': self._running,
            'candidates_generated_today': self._candidates_generated_today,
            'candidates_validated_today': self._candidates_validated_today,
            'genetic_search': self.genetic_search.get_stats(),
            'factor_library': self.factor_library.get_stats(),
        }


# Singleton instance
_alpha_mining_engine: Optional[AlphaMiningEngine] = None


def get_alpha_mining_engine() -> AlphaMiningEngine:
    """Get the singleton Alpha Mining Engine."""
    global _alpha_mining_engine
    if _alpha_mining_engine is None:
        _alpha_mining_engine = AlphaMiningEngine()
    return _alpha_mining_engine
