    async def analyze_strategy(
        self,
        strategy_id: str,
        strategy_code: str,
        historical_data: Dict[str, Any],
    ) -> VulnerabilityReport:
        """
        Analyze a strategy for vulnerabilities.
        
        Args:
            strategy_id: Unique strategy identifier
            strategy_code: Strategy implementation code
            historical_data: Historical performance data
        
        Returns:
            VulnerabilityReport with findings
        """
        vulnerabilities = []
        attack_vectors = {}
        
        # Test each attack type
        for attack_type in AttackType:
            logger.debug(f"Testing {attack_type.value} against {strategy_id}")
            
            # Generate multiple variants of this attack
            success_count = 0
            test_count = 5
            
            for _ in range(test_count):
                attack = await self.generate_attack(attack_type, strategy_id)
                
                # Simulate attack (would normally run against strategy)
                damage = await self._simulate_attack(attack, strategy_code, historical_data)
                
                if damage > 0.3:  # Significant damage threshold
                    success_count += 1
                    vulnerabilities.append({
                        'attack_type': attack_type.value,
                        'attack_id': attack.attack_id,
                        'damage': damage,
                        'severity': attack.severity,
                    })
            
            attack_vectors[attack_type] = success_count / test_count
        
        # Calculate overall scores
        exploitability = np.mean(list(attack_vectors.values()))
        severity = np.mean([v['damage'] for v in vulnerabilities]) if vulnerabilities else 0
        
        # Determine recommended defenses
        recommended = self._recommend_defenses(vulnerabilities)
        
        report = VulnerabilityReport(
            report_id=f"VULN-{uuid.uuid4().hex[:12]}",
            strategy_id=strategy_id,
            vulnerabilities=vulnerabilities,
            exploitability_score=exploitability,
            severity_score=severity,
            recommended_defenses=recommended,
            attack_vectors=attack_vectors,
            blind_spots=self._identify_blind_spots(attack_vectors),
            failure_modes=self._identify_failure_modes(vulnerabilities),
        )
        
        # Store report
        if strategy_id not in self.vulnerability_database:
            self.vulnerability_database[strategy_id] = []
        self.vulnerability_database[strategy_id].append(report)
        
        logger.info(f"🔴 Vulnerability analysis complete for {strategy_id}: "
                   f"{len(vulnerabilities)} vulnerabilities, exploitability={exploitability:.2f}")
        
        return report
    
