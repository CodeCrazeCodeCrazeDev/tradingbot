class BattleArena:
    """
    Battle Arena - Simulates Red vs Blue battles.
    
    Features:
    - Simulates attack-defense scenarios
    - Tracks win/loss statistics
    - Generates battle reports
    - Continuous training loop
    """
    
    def __init__(self, red_team: RedTeam, blue_team: BlueTeam, config: Optional[Dict] = None):
        self.red_team = red_team
        self.blue_team = blue_team
        self.config = config or {}
        
        self.battle_history: List[Battle] = []
        self.strategy_robustness: Dict[str, float] = {}
        
        logger.info("⚔️ Battle Arena initialized")
    
    async def run_battle(
        self,
        strategy_id: str,
        attack_type: Optional[AttackType] = None,
        defense_types: Optional[List[DefenseType]] = None,
    ) -> Battle:
        """
        Run a single battle between Red and Blue teams.
        
        Args:
            strategy_id: Strategy under test
            attack_type: Specific attack type (or random if None)
            defense_types: Specific defenses (or auto-selected if None)
        
        Returns:
            Battle result
        """
        # Generate attack
        if attack_type is None:
            attack_type = random.choice(list(AttackType))
        
        attack = await self.red_team.generate_attack(attack_type, strategy_id)
        
        # Deploy defenses
        if defense_types is None:
            # Auto-select based on attack
            defense_types = self._select_defenses(attack_type)
        
        defenses = []
        for defense_type in defense_types:
            defense = await self.blue_team.deploy_defense(defense_type, strategy_id)
            defenses.append(defense)
        
        # Combine defenses into one (simplified)
        combined_defense = self._combine_defenses(defenses, strategy_id)
        
        # Create scenario
        scenario = self._create_scenario(attack, combined_defense)
        
        # Run battle simulation
        outcome, damage, effectiveness = await self._simulate_battle(
            attack, combined_defense, scenario
        )
        
        # Create battle record
        battle = Battle(
            battle_id=f"BTL-{uuid.uuid4().hex[:12]}",
            attack=attack,
            defense=combined_defense,
            scenario=scenario,
            outcome=outcome,
            actual_damage=damage,
            defense_effectiveness=effectiveness,
            battle_duration_ms=random.uniform(10, 1000),
        )
        
        self.battle_history.append(battle)
        
        # Update strategy robustness
        current_robustness = self.strategy_robustness.get(strategy_id, 0.5)
        if outcome == BattleOutcome.BLUE_WINS:
            self.strategy_robustness[strategy_id] = min(1.0, current_robustness + 0.05)
        elif outcome == BattleOutcome.RED_WINS:
            self.strategy_robustness[strategy_id] = max(0.0, current_robustness - 0.1)
        
        logger.info(f"⚔️ Battle {battle.battle_id}: {outcome.value} "
                   f"(damage={damage:.2f}, effectiveness={effectiveness:.2f})")
        
        return battle
    
    async def run_tournament(
        self,
        strategy_id: str,
        num_battles: int = 10,
    ) -> Dict[str, Any]:
        """
        Run a tournament of multiple battles.
        
        Args:
            strategy_id: Strategy to test
            num_battles: Number of battles to run
        
        Returns:
            Tournament statistics
        """
        results = {
            'strategy_id': strategy_id,
            'total_battles': num_battles,
            'wins': {BattleOutcome.RED_WINS: 0, BattleOutcome.BLUE_WINS: 0, BattleOutcome.DRAW: 0},
            'total_damage': 0.0,
            'avg_defense_effectiveness': 0.0,
            'battles': [],
        }
        
        for i in range(num_battles):
            battle = await self.run_battle(strategy_id)
            results['wins'][battle.outcome] += 1
            results['total_damage'] += battle.actual_damage
            results['avg_defense_effectiveness'] += battle.defense_effectiveness
            results['battles'].append(battle.to_dict())
        
        results['avg_damage'] = results['total_damage'] / num_battles
        results['avg_defense_effectiveness'] /= num_battles
        results['win_rate'] = results['wins'][BattleOutcome.BLUE_WINS] / num_battles
        
        logger.info(f"🏆 Tournament complete for {strategy_id}: "
                   f"win_rate={results['win_rate']:.2f}, avg_damage={results['avg_damage']:.2f}")
        
        return results
    
    def _select_defenses(self, attack_type: AttackType) -> List[DefenseType]:
        """Select appropriate defenses for an attack type."""
        defense_map = {
            AttackType.DATA_POISONING: [DefenseType.INPUT_VALIDATION, DefenseType.ANOMALY_DETECTION],
            AttackType.ADVERSARIAL_INPUT: [DefenseType.SANITY_CHECK, DefenseType.INPUT_VALIDATION],
            AttackType.STRATEGY_INVERSION: [DefenseType.ENSEMBLE_FALLBACK, DefenseType.RISK_FILTER],
            AttackType.MARKET_MANIPULATION: [DefenseType.CIRCUIT_BREAKER, DefenseType.ANOMALY_DETECTION],
            AttackType.FLASH_CRASH: [DefenseType.CIRCUIT_BREAKER, DefenseType.DYNAMIC_STOP],
            AttackType.LIQUIDITY_SQUEEZE: [DefenseType.GRADUAL_ENTRY, DefenseType.MARGIN_BUFFER],
            AttackType.CORRELATION_BREAKDOWN: [DefenseType.DIVERSIFICATION],
            AttackType.REGIME_SHIFT: [DefenseType.ENSEMBLE_FALLBACK],
            AttackType.PARAMETER_DRIFT: [DefenseType.SANITY_CHECK, DefenseType.ANOMALY_DETECTION],
            AttackType.EXECUTION_DELAY: [DefenseType.CIRCUIT_BREAKER],
        }
        
        return defense_map.get(attack_type, [DefenseType.RISK_FILTER])
    
    def _combine_defenses(self, defenses: List[Defense], strategy_id: str) -> Defense:
        """Combine multiple defenses into one."""
        if not defenses:
            return Defense(
                defense_id=f"DEF-{uuid.uuid4().hex[:12]}",
                defense_type=DefenseType.RISK_FILTER,
                protected_strategy=strategy_id,
                parameters={},
                strength=0.0,
                coverage=[],
                resource_cost=0.0,
            )
        
        if len(defenses) == 1:
            return defenses[0]
        
        # Combine parameters
        combined_params = {}
        for d in defenses:
            combined_params.update(d.parameters)
        
        # Average strength (with diminishing returns)
        raw_strength = np.mean([d.strength for d in defenses])
        combined_strength = raw_strength * (1 - 0.1 * (len(defenses) - 1))  # Diminishing returns
        
        # Union of coverage
        combined_coverage = list(set().union(*[d.coverage for d in defenses]))
        
        # Sum resource cost
        combined_cost = sum(d.resource_cost for d in defenses)
        
        return Defense(
            defense_id=f"DEF-COMBINED-{uuid.uuid4().hex[:8]}",
            defense_type=DefenseType.RISK_FILTER,  # Generic combined type
            protected_strategy=strategy_id,
            parameters=combined_params,
            strength=combined_strength,
            coverage=combined_coverage,
            resource_cost=combined_cost,
        )
    
    def _create_scenario(self, attack: Attack, defense: Defense) -> Dict[str, Any]:
        """Create a battle scenario."""
        return {
            'market_conditions': random.choice(['normal', 'volatile', 'trending', 'choppy']),
            'liquidity': random.uniform(0.3, 0.9),
            'volatility': random.uniform(0.1, 0.5),
            'attack_intensity': attack.severity,
            'defense_strength': defense.strength,
        }
    
    async def _simulate_battle(
        self,
        attack: Attack,
        defense: Defense,
        scenario: Dict[str, Any],
    ) -> Tuple[BattleOutcome, float, float]:
        """Simulate battle and return outcome."""
        # Calculate effectiveness
        attack_power = attack.severity * attack.expected_impact * (1 + attack.complexity * 0.2)
        defense_power = defense.strength * (1 - defense.resource_cost * 0.1)
        
        # Adjust for scenario
        if scenario['market_conditions'] == 'volatile':
            attack_power *= 1.2
        elif scenario['market_conditions'] == 'normal':
            defense_power *= 1.1
        
        # Check coverage
        if attack.attack_type in defense.coverage:
            defense_power *= 1.3
        else:
            defense_power *= 0.7
        
        # Add randomness
        attack_roll = attack_power * random.uniform(0.8, 1.2)
        defense_roll = defense_power * random.uniform(0.8, 1.2)
        
        # Determine outcome
        margin = defense_roll - attack_roll
        
        if margin > 0.2:
            outcome = BattleOutcome.BLUE_WINS
            damage = max(0, attack.expected_impact - margin * 0.5)
        elif margin < -0.2:
            outcome = BattleOutcome.RED_WINS
            damage = min(1, attack.expected_impact + abs(margin) * 0.3)
        else:
            outcome = BattleOutcome.DRAW
            damage = attack.expected_impact * 0.5
        
        effectiveness = defense_roll / (defense_roll + attack_roll + 1e-10)
        
        return outcome, damage, effectiveness
    
    def get_battle_statistics(self) -> Dict[str, Any]:
        """Get statistics about all battles."""
        if not self.battle_history:
            return {'total_battles': 0}
        
        outcomes = [b.outcome for b in self.battle_history]
        
        return {
            'total_battles': len(self.battle_history),
            'red_wins': outcomes.count(BattleOutcome.RED_WINS),
            'blue_wins': outcomes.count(BattleOutcome.BLUE_WINS),
            'draws': outcomes.count(BattleOutcome.DRAW),
            'avg_damage': np.mean([b.actual_damage for b in self.battle_history]),
            'avg_defense_effectiveness': np.mean([b.defense_effectiveness for b in self.battle_history]),
            'strategy_robustness': self.strategy_robustness.copy(),
        }


# Integration class
class AdversarialFramework:
    """
    Complete Red Team / Blue Team adversarial framework.
    
    Provides:
    - Vulnerability analysis
    - Defense deployment
    - Battle simulation
    - Continuous improvement
    """
    
    def __init__(self, config: Optional[Dict] = None):
        self.config = config or {}
        
        self.red_team = RedTeam(config)
        self.blue_team = BlueTeam(config)
        self.arena = BattleArena(self.red_team, self.blue_team, config)
        
        logger.info("🛡️ Adversarial Framework initialized")
    
    async def harden_strategy(
        self,
        strategy_id: str,
        strategy_code: str,
        historical_data: Dict[str, Any],
        num_tournament_battles: int = 20,
    ) -> Dict[str, Any]:
        """
        Complete hardening process for a strategy.
        
        Args:
            strategy_id: Strategy identifier
            strategy_code: Strategy implementation
            historical_data: Historical performance
            num_tournament_battles: Number of battles for tournament
        
        Returns:
            Complete hardening report
        """
        logger.info(f"🛡️ Starting hardening process for {strategy_id}")
        
        # Phase 1: Vulnerability analysis
        vuln_report = await self.red_team.analyze_strategy(
            strategy_id, strategy_code, historical_data
        )
        
        # Phase 2: Defense deployment
        robustness_report = await self.blue_team.defend_strategy(
            strategy_id, vuln_report
        )
        
        # Phase 3: Tournament
        tournament_results = await self.arena.run_tournament(
            strategy_id, num_battles=num_tournament_battles
        )
        
        # Compile report
        report = {
            'strategy_id': strategy_id,
            'timestamp': datetime.now(timezone.utc).isoformat(),
            'vulnerability_summary': {
                'exploitability': vuln_report.exploitability_score,
                'severity': vuln_report.severity_score,
                'num_vulnerabilities': len(vuln_report.vulnerabilities),
                'blind_spots': vuln_report.blind_spots,
                'failure_modes': vuln_report.failure_modes,
            },
            'defense_summary': {
                'robustness': robustness_report.overall_robustness,
                'num_defenses': len(robustness_report.active_defenses),
                'coverage': {
                    at.value: cov for at, cov in robustness_report.defense_coverage.items()
                },
                'gaps': [at.value for at in robustness_report.defense_gaps],
            },
            'tournament_results': {
                'win_rate': tournament_results['win_rate'],
                'avg_damage': tournament_results['avg_damage'],
                'total_battles': tournament_results['total_battles'],
            },
            'final_robustness_score': self.arena.strategy_robustness.get(strategy_id, 0),
        }
        
        logger.info(f"🛡️ Hardening complete for {strategy_id}: "
                   f"robustness={report['final_robustness_score']:.2f}")
        
        return report
    
    def get_framework_statistics(self) -> Dict[str, Any]:
        """Get overall framework statistics."""
        return {
            'red_team': self.red_team.get_attack_statistics(),
            'arena': self.arena.get_battle_statistics(),
        }


