class BattleOutcome(Enum):
    """Outcome of a Red vs Blue battle."""
    RED_WINS = "red_wins"  # Attack succeeded
    BLUE_WINS = "blue_wins"  # Defense succeeded
    DRAW = "draw"  # Both neutralized
    UNKNOWN = "unknown"  # Could not determine


@dataclass
class Attack:
    """An attack generated by the Red Team."""
    attack_id: str
    attack_type: AttackType
    target_strategy: str
    parameters: Dict[str, Any]
    severity: float  # 0-1
    expected_impact: float  # Expected damage
    complexity: float  # 0-1, how sophisticated
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    
    def to_dict(self) -> Dict[str, Any]:
        return {
            'attack_id': self.attack_id,
            'attack_type': self.attack_type.value,
            'target_strategy': self.target_strategy,
            'parameters': self.parameters,
            'severity': self.severity,
            'expected_impact': self.expected_impact,
            'complexity': self.complexity,
            'timestamp': self.timestamp.isoformat(),
        }


@dataclass
class Defense:
    """A defense deployed by the Blue Team."""
    defense_id: str
    defense_type: DefenseType
    protected_strategy: str
    parameters: Dict[str, Any]
    strength: float  # 0-1
    coverage: List[AttackType]  # Which attacks this defends against
    resource_cost: float  # Computational cost
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    
    def to_dict(self) -> Dict[str, Any]:
        return {
            'defense_id': self.defense_id,
            'defense_type': self.defense_type.value,
            'protected_strategy': self.protected_strategy,
            'parameters': self.parameters,
            'strength': self.strength,
            'coverage': [at.value for at in self.coverage],
            'resource_cost': self.resource_cost,
            'timestamp': self.timestamp.isoformat(),
        }


@dataclass
class Battle:
    """A battle between Red Team attack and Blue Team defense."""
    battle_id: str
    attack: Attack
    defense: Defense
    scenario: Dict[str, Any]
    
    # Results
    outcome: BattleOutcome = BattleOutcome.UNKNOWN
    actual_damage: float = 0.0  # 0-1
    defense_effectiveness: float = 0.0  # 0-1
    battle_duration_ms: float = 0.0
    
    # Analysis
    attack_success_factors: List[str] = field(default_factory=list)
    defense_failure_reasons: List[str] = field(default_factory=list)
    
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    
    def to_dict(self) -> Dict[str, Any]:
        return {
            'battle_id': self.battle_id,
            'attack': self.attack.to_dict(),
            'defense': self.defense.to_dict(),
            'scenario': self.scenario,
            'outcome': self.outcome.value,
            'actual_damage': self.actual_damage,
            'defense_effectiveness': self.defense_effectiveness,
            'battle_duration_ms': self.battle_duration_ms,
            'attack_success_factors': self.attack_success_factors,
            'defense_failure_reasons': self.defense_failure_reasons,
            'timestamp': self.timestamp.isoformat(),
        }


@dataclass
class VulnerabilityReport:
    """Report of vulnerabilities found by Red Team."""
    report_id: str
    strategy_id: str
    vulnerabilities: List[Dict[str, Any]]
    exploitability_score: float  # 0-1
    severity_score: float  # 0-1
    recommended_defenses: List[DefenseType]
    
    # Detailed analysis
    attack_vectors: Dict[AttackType, float]  # Type -> success rate
    blind_spots: List[str]
    failure_modes: List[str]
    
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


@dataclass
class RobustnessReport:
    """Report of robustness verified by Blue Team."""
    report_id: str
    strategy_id: str
    defense_coverage: Dict[AttackType, float]  # Type -> coverage %
    overall_robustness: float  # 0-1
    stress_test_results: List[Dict[str, Any]]
    
    # Defense effectiveness
    active_defenses: List[Defense]
    defense_gaps: List[AttackType]
    
    # Recommendations
    suggested_improvements: List[str]
    priority_defenses: List[DefenseType]
    
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


