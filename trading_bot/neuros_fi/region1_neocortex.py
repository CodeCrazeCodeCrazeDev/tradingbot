"""
NEUROS-FI Region 1: Neocortex - Six-Layer Predictive Coding Hierarchy
======================================================================

Biological Basis:
The cortex runs a hierarchy of prediction machines. Layer IV receives bottom-up
sensory data. Layers II/III transmit lateral predictions. Layers V/VI send
top-down predictions back down. Only prediction errors propagate upward — not
raw data. This is maximally efficient: the system transmits only surprise.

Citations:
- Rao & Ballard (1999) - Predictive coding in the visual cortex
- Friston (2005) - A theory of cortical responses
- Bastos et al. (2012) - Canonical microcircuits for predictive coding
- Clark (2013) - Whatever next? Predictive brains, situated agents

Cortical Layer Mapping:
- Layer VI: Decades (monetary regime priors, secular cycles)
- Layer V: Years-Months (portfolio construction, strategic regime)
- Layer IV: Weeks-Days (thalamic input relay)
- Layer III: Days-Hours (cross-asset pattern recognition)
- Layer II: Hours-Minutes (intraday signal processing)
- Layer I: Minutes-Nanoseconds (execution response)

Constitutional Version: 5.0
"""

