class LayerIV_ThalamicRelay(CorticalLayer):
    """
    Layer IV: Weeks-Days - Thalamic Input Relay
    
    The gateway layer - all sensory input passes through here.
    Integrates signals from all data feeds before cortical processing.
    """
    
    def __init__(self):
        super().__init__(
            layer_type=CorticalLayerType.LAYER_IV,
            time_horizon=timedelta(weeks=1),
            precision_prior=2.0
        )
        
        # Input relay state
        self._data_feeds: Dict[str, Dict[str, Any]] = {}
        self._feed_quality: Dict[str, float] = {}
        self._integration_weights: Dict[str, float] = {}
    
    def generate_prediction(
        self,
        input_data: Dict[str, Any],
        top_down_context: Optional[Dict[str, Any]] = None
    ) -> Prediction:
        """Generate integrated market state prediction."""
        
        # Integrate multiple data feeds
        feeds = input_data.get('feeds', {})
        
        # Quality-weighted integration
        integrated_signal = 0.0
        total_weight = 0.0
        
        for feed_id, feed_data in feeds.items():
            quality = self._feed_quality.get(feed_id, 0.5)
            weight = self._integration_weights.get(feed_id, 1.0) * quality
            
            signal = feed_data.get('signal', 0)
            integrated_signal += signal * weight
            total_weight += weight
        
        if total_weight > 0:
            integrated_signal /= total_weight
        
        return Prediction(
            layer=self.layer_type,
            timestamp=datetime.utcnow(),
            target_variable='integrated_signal',
            predicted_value=integrated_signal,
            confidence=0.4,
            precision=self.precision_prior,
            time_horizon=self.time_horizon,
            context={'feed_count': len(feeds)},
        )
    
    def process_bottom_up_input(
        self,
        input_data: Dict[str, Any]
    ) -> Optional[PredictionError]:
        """Process integrated signal outcome."""
        
        actual_signal = input_data.get('actual_integrated_signal', 0)
        prediction = self._predictions.get('integrated_signal')
        
        if prediction is None:
            return None
        
        error_magnitude = abs(actual_signal - prediction.predicted_value)
        precision_weighted = error_magnitude * prediction.precision
        
        return PredictionError(
            error_type=PredictionErrorType.SENSORY,
            source_layer=self.layer_type,
            timestamp=datetime.utcnow(),
            prediction=prediction,
            observed_value=actual_signal,
            error_magnitude=error_magnitude,
            precision_weighted_error=precision_weighted,
            surprise=0.0,
        )
    
    def update_feed_quality(self, feed_id: str, quality: float):
        """Update the quality score for a data feed."""
        self._feed_quality[feed_id] = max(0.0, min(1.0, quality))


class LayerV_Portfolio(CorticalLayer):
    """
    Layer V: Years-Months - Portfolio Construction
    
    Strategic regime models and cross-strategy capital allocation.
    Output layer to portfolio construction.
    """
    
    def __init__(self):
        super().__init__(
            layer_type=CorticalLayerType.LAYER_V,
            time_horizon=timedelta(days=90),
            precision_prior=1.0
        )
        
        # Portfolio state
        self._strategy_allocations: Dict[str, float] = {}
        self._regime_state: str = "normal"
        self._risk_budget: float = 1.0
    
    def generate_prediction(
        self,
        input_data: Dict[str, Any],
        top_down_context: Optional[Dict[str, Any]] = None
    ) -> Prediction:
        """Generate strategic portfolio prediction."""
        
        # Extract strategy performance
        strategy_returns = input_data.get('strategy_returns', {})
        regime = input_data.get('regime', 'normal')
        
        # Regime-conditional expected return
        regime_multipliers = {
            'bull': 1.2,
            'normal': 1.0,
            'bear': 0.6,
            'crisis': 0.2,
        }
        
        base_return = sum(strategy_returns.values()) / max(len(strategy_returns), 1)
        regime_adjusted = base_return * regime_multipliers.get(regime, 1.0)
        
        # Adjust for secular context from Layer VI
        if top_down_context:
            secular_adjustment = top_down_context.get('secular_adjustment', 1.0)
            regime_adjusted *= secular_adjustment
        
        self._regime_state = regime
        
        return Prediction(
            layer=self.layer_type,
            timestamp=datetime.utcnow(),
            target_variable='portfolio_return',
            predicted_value=regime_adjusted,
            confidence=0.3,
            precision=self.precision_prior,
            time_horizon=self.time_horizon,
            context={'regime': regime},
        )
    
    def process_bottom_up_input(
        self,
        input_data: Dict[str, Any]
    ) -> Optional[PredictionError]:
        """Process actual portfolio returns."""
        
        actual_return = input_data.get('actual_portfolio_return', 0)
        prediction = self._predictions.get('portfolio_return')
        
        if prediction is None:
            return None
        
        error_magnitude = abs(actual_return - prediction.predicted_value)
        precision_weighted = error_magnitude * prediction.precision
        
        return PredictionError(
            error_type=PredictionErrorType.SENSORY,
            source_layer=self.layer_type,
            timestamp=datetime.utcnow(),
            prediction=prediction,
            observed_value=actual_return,
            error_magnitude=error_magnitude,
            precision_weighted_error=precision_weighted,
            surprise=0.0,
        )
    
    def get_regime_adjustment(self) -> float:
        """Get regime adjustment for lower layers."""
        adjustments = {
            'bull': 1.2,
            'normal': 1.0,
            'bear': 0.7,
            'crisis': 0.3,
        }
        return adjustments.get(self._regime_state, 1.0)


class LayerVI_Secular(CorticalLayer):
    """
    Layer VI: Decades - Monetary Regime Priors
    
    The slowest layer, holding secular demographic cycles and
    long-run factor models. Provides top-down context to all layers.
    """
    
    def __init__(self):
        super().__init__(
            layer_type=CorticalLayerType.LAYER_VI,
            time_horizon=timedelta(days=3650),  # ~10 years
            precision_prior=0.5  # Low precision for long-horizon
        )
        
        # Secular state
        self._monetary_regime: str = "normal"
        self._secular_cycle_phase: str = "expansion"
        self._demographic_factors: Dict[str, float] = {}
        self._long_run_priors: Dict[str, float] = {
            'equity_premium': 0.05,
            'bond_return': 0.02,
            'inflation': 0.02,
        }
    
    def generate_prediction(
        self,
        input_data: Dict[str, Any],
        top_down_context: Optional[Dict[str, Any]] = None
    ) -> Prediction:
        """Generate secular/long-run prediction."""
        
        # Extract macro indicators
        inflation = input_data.get('inflation', 0.02)
        growth = input_data.get('gdp_growth', 0.02)
        policy_rate = input_data.get('policy_rate', 0.03)
        
