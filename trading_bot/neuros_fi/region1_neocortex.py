class CorticalLayerType(Enum):
    """The six cortical layers with their temporal horizons."""
    
    LAYER_I = auto()    # Minutes-Nanoseconds: Execution response
    LAYER_II = auto()   # Hours-Minutes: Intraday signals
    LAYER_III = auto()  # Days-Hours: Cross-asset patterns
    LAYER_IV = auto()   # Weeks-Days: Thalamic input relay
    LAYER_V = auto()    # Years-Months: Portfolio construction
    LAYER_VI = auto()   # Decades: Monetary regime priors


class PredictionErrorType(Enum):
    """Types of prediction errors in the hierarchy."""
    
    SENSORY = auto()      # Bottom-up sensory mismatch
    PRECISION = auto()    # Uncertainty in prediction
    TEMPORAL = auto()     # Timing mismatch
    STRUCTURAL = auto()   # Model structure mismatch


@dataclass
class Prediction:
    """A prediction generated by a cortical layer."""
    
    layer: CorticalLayerType
    timestamp: datetime
    target_variable: str
    predicted_value: float
    confidence: float  # 0-1
    precision: float   # Inverse variance (higher = more certain)
    time_horizon: timedelta
    context: Dict[str, Any] = field(default_factory=dict)
    prediction_id: str = ""
    
    def __post_init__(self):
        if not self.prediction_id:
            self.prediction_id = f"pred_{self.layer.name}_{int(time.time()*1000)}"


@dataclass
class PredictionError:
    """A prediction error that propagates upward through the hierarchy."""
    
    error_type: PredictionErrorType
    source_layer: CorticalLayerType
    timestamp: datetime
    prediction: Prediction
    observed_value: float
    error_magnitude: float  # Absolute error
    precision_weighted_error: float  # Error weighted by precision
    surprise: float  # -log p(observation | prediction)
    propagate_upward: bool = True
    
    def __post_init__(self):
        # Calculate surprise (negative log probability)
        # Higher precision predictions that are wrong generate more surprise
        if self.error_magnitude > 0:
            self.surprise = self.precision_weighted_error * math.log(1 + self.error_magnitude)


@dataclass
class CorticalColumnState:
    """State of a cortical column (asset-class specialized processor)."""
    
    column_id: str
    asset_class: str
    active_predictions: List[Prediction] = field(default_factory=list)
    pending_errors: List[PredictionError] = field(default_factory=list)
    layer_activations: Dict[CorticalLayerType, float] = field(default_factory=dict)
    last_update: datetime = field(default_factory=datetime.utcnow)


class CorticalLayer(ABC):
    """
    Abstract base class for a cortical layer in the predictive coding hierarchy.
    
    Each layer:
    1. Receives bottom-up input from the layer below
    2. Generates predictions for the layer below
    3. Receives top-down predictions from the layer above
    4. Computes prediction errors and propagates them upward
    """
    
    def __init__(
        self,
        layer_type: CorticalLayerType,
        time_horizon: timedelta,
        precision_prior: float = 1.0
    ):
        self.layer_type = layer_type
        self.time_horizon = time_horizon
        self.precision_prior = precision_prior
        
        # Layer state
        self._predictions: Dict[str, Prediction] = {}
        self._errors: List[PredictionError] = []
        self._weights: Dict[str, float] = {}
        self._learning_rate: float = 0.01
        
        # Connections
        self._layer_above: Optional['CorticalLayer'] = None
        self._layer_below: Optional['CorticalLayer'] = None
        
        # Plasticity state (for STDP)
        self._recent_activations: List[Tuple[datetime, float]] = []
        self._plasticity_window = timedelta(seconds=1)
    
    @abstractmethod
    def generate_prediction(
        self,
        input_data: Dict[str, Any],
        top_down_context: Optional[Dict[str, Any]] = None
    ) -> Prediction:
        """Generate a prediction for the layer below."""
        pass
    
    @abstractmethod
    def process_bottom_up_input(
        self,
        input_data: Dict[str, Any]
    ) -> Optional[PredictionError]:
        """Process input from the layer below, compute prediction error."""
        pass
    
    def receive_top_down_prediction(self, prediction: Prediction):
        """Receive a prediction from the layer above as context."""
        self._predictions[prediction.target_variable] = prediction
    
    def propagate_error_upward(self, error: PredictionError):
        """Propagate a prediction error to the layer above."""
        if self._layer_above and error.propagate_upward:
            self._layer_above.receive_error_from_below(error)
    
    def receive_error_from_below(self, error: PredictionError):
        """Receive an error from the layer below for learning."""
        self._errors.append(error)
        self._update_weights(error)
    
    def _update_weights(self, error: PredictionError):
        """Update weights based on prediction error (Hebbian learning)."""
        # Precision-weighted error drives learning
        learning_signal = error.precision_weighted_error * self._learning_rate
        
        # Update relevant weights
        for key in self._weights:
            if key in error.prediction.context:
                # Hebbian: strengthen connections that predicted correctly
                # Anti-Hebbian: weaken connections that predicted incorrectly
                self._weights[key] -= learning_signal * error.prediction.context[key]
    
    def connect_to_layer_above(self, layer: 'CorticalLayer'):
        """Connect to the layer above in the hierarchy."""
        self._layer_above = layer
        layer._layer_below = self
    
    def get_free_energy(self) -> float:
        """
        Calculate the free energy (total prediction error) for this layer.
        
        F = D_KL[q(s) || p(s|o)] - ln p(o)
        
        In practice: sum of precision-weighted squared errors
        """
        if not self._errors:
            return 0.0
        
        return sum(e.precision_weighted_error ** 2 for e in self._errors[-100:])


class LayerI_Execution(CorticalLayer):
    """
    Layer I: Minutes-Nanoseconds - Execution Response
    
    The fastest layer, handling microstructure adaptation and order routing.
    Runs in co-located execution facility.
    """
    
    def __init__(self):
        super().__init__(
            layer_type=CorticalLayerType.LAYER_I,
            time_horizon=timedelta(minutes=1),
            precision_prior=10.0  # High precision for execution
        )
        
        # Execution-specific state
        self._order_book_state: Dict[str, Any] = {}
        self._microstructure_model: Dict[str, float] = {}
        self._execution_latency_ms: float = 0.0
    
    def generate_prediction(
        self,
        input_data: Dict[str, Any],
        top_down_context: Optional[Dict[str, Any]] = None
    ) -> Prediction:
        """Predict execution outcome (fill probability, slippage)."""
        
        # Extract order details
        order_size = input_data.get('order_size', 0)
        spread = input_data.get('spread', 0.0001)
        volatility = input_data.get('volatility', 0.01)
        
        # Simple execution cost prediction
        predicted_slippage = spread * 0.5 + volatility * order_size * 0.0001
        
        return Prediction(
            layer=self.layer_type,
            timestamp=datetime.utcnow(),
            target_variable='execution_cost',
            predicted_value=predicted_slippage,
            confidence=0.8,
            precision=self.precision_prior,
            time_horizon=self.time_horizon,
            context=input_data,
        )
    
    def process_bottom_up_input(
        self,
        input_data: Dict[str, Any]
    ) -> Optional[PredictionError]:
        """Process actual execution outcome, compute error."""
        
        actual_cost = input_data.get('actual_execution_cost', 0)
        prediction = self._predictions.get('execution_cost')
        
        if prediction is None:
            return None
        
        error_magnitude = abs(actual_cost - prediction.predicted_value)
        precision_weighted = error_magnitude * prediction.precision
        
        return PredictionError(
            error_type=PredictionErrorType.SENSORY,
            source_layer=self.layer_type,
            timestamp=datetime.utcnow(),
            prediction=prediction,
            observed_value=actual_cost,
            error_magnitude=error_magnitude,
            precision_weighted_error=precision_weighted,
            surprise=0.0,  # Calculated in __post_init__
        )


class LayerII_Intraday(CorticalLayer):
    """
    Layer II: Hours-Minutes - Intraday Signal Processing
    
    Short-horizon alpha models and real-time signal integration.
    """
    
    def __init__(self):
        super().__init__(
            layer_type=CorticalLayerType.LAYER_II,
            time_horizon=timedelta(hours=4),
            precision_prior=5.0
        )
        
        # Intraday signal state
        self._active_signals: Dict[str, float] = {}
        self._signal_weights: Dict[str, float] = {}
    
    def generate_prediction(
        self,
        input_data: Dict[str, Any],
        top_down_context: Optional[Dict[str, Any]] = None
    ) -> Prediction:
        """Generate intraday price movement prediction."""
        
        # Aggregate signals
        signals = input_data.get('signals', {})
        weights = self._signal_weights
        
        # Weighted signal combination
        if signals and weights:
            prediction_value = sum(
                signals.get(k, 0) * weights.get(k, 1.0)
                for k in signals
            ) / max(len(signals), 1)
        else:
            prediction_value = 0.0
        
        # Adjust for top-down context (regime from Layer V)
        if top_down_context:
            regime_adjustment = top_down_context.get('regime_adjustment', 1.0)
            prediction_value *= regime_adjustment
        
        return Prediction(
            layer=self.layer_type,
            timestamp=datetime.utcnow(),
            target_variable='intraday_return',
            predicted_value=prediction_value,
            confidence=0.6,
            precision=self.precision_prior,
            time_horizon=self.time_horizon,
            context={'signals': signals},
        )
    
    def process_bottom_up_input(
        self,
        input_data: Dict[str, Any]
    ) -> Optional[PredictionError]:
        """Process actual intraday returns, compute error."""
        
        actual_return = input_data.get('actual_return', 0)
        prediction = self._predictions.get('intraday_return')
        
        if prediction is None:
            return None
        
        error_magnitude = abs(actual_return - prediction.predicted_value)
        precision_weighted = error_magnitude * prediction.precision
        
        error = PredictionError(
            error_type=PredictionErrorType.SENSORY,
            source_layer=self.layer_type,
            timestamp=datetime.utcnow(),
            prediction=prediction,
            observed_value=actual_return,
            error_magnitude=error_magnitude,
            precision_weighted_error=precision_weighted,
            surprise=0.0,
        )
        
        # Propagate significant errors upward
        if error_magnitude > 0.01:  # 1% threshold
            error.propagate_upward = True
        
        return error


