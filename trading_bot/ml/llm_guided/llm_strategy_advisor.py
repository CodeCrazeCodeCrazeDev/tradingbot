"""
LLM-Guided Reinforcement Learning

Uses GPT-4/Claude to provide natural language strategy advice and explanations.
"""

import json
from typing import Dict, List, Optional
import logging

logger = logging.getLogger(__name__)


class LLMStrategyAdvisor:
    """
    LLM-based strategy advisor
    
    Capabilities:
    - Natural language market analysis
    - Strategy suggestions
    - Trade explanations
    - Risk warnings
    """
    
    def __init__(self, api_key: Optional[str] = None, model: str = "gpt-4"):
        """
        Args:
            api_key: OpenAI/Anthropic API key
            model: Model to use ('gpt-4', 'claude-3', etc.)
        """
        self.api_key = api_key
        self.model = model
        self.client = None
        
        if api_key:
            self._initialize_client()
    
    def _initialize_client(self):
        """Initialize LLM client"""
        try:
            if 'gpt' in self.model.lower():
                import openai
                self.client = openai.OpenAI(api_key=self.api_key)
                logger.info(f"Initialized OpenAI client with model: {self.model}")
            elif 'claude' in self.model.lower():
                import anthropic
                self.client = anthropic.Anthropic(api_key=self.api_key)
                logger.info(f"Initialized Anthropic client with model: {self.model}")
        except ImportError:
            logger.warning("LLM client library not installed. Using mock responses.")
            self.client = None
    
    def analyze_market(
        self,
        market_data: Dict,
        technical_indicators: Dict,
        news_sentiment: Optional[str] = None
    ) -> str:
        """
        Get LLM analysis of current market conditions
        
        Args:
            market_data: Current market data
            technical_indicators: Technical indicators
            news_sentiment: Recent news sentiment
            
        Returns:
            Natural language market analysis
        """
        prompt = self._build_market_analysis_prompt(
            market_data, technical_indicators, news_sentiment
        )
        
        response = self._query_llm(prompt)
        
        logger.info("Market analysis generated by LLM")
        
        return response
    
    def suggest_strategy(
        self,
        market_regime: str,
        portfolio_state: Dict,
        risk_tolerance: str = "moderate"
    ) -> Dict:
        """
        Get strategy suggestions from LLM
        
        Args:
            market_regime: Current market regime
            portfolio_state: Current portfolio state
            risk_tolerance: Risk tolerance level
            
        Returns:
            Strategy suggestions
        """
        prompt = f"""
        You are an expert trading strategist. Analyze the following situation and provide strategy recommendations.
        
        Market Regime: {market_regime}
        Portfolio State: {json.dumps(portfolio_state, indent=2)}
        Risk Tolerance: {risk_tolerance}
        
        Provide:
        1. Recommended strategy (aggressive/conservative/neutral)
        2. Position sizing guidance
        3. Key risks to watch
        4. Entry/exit criteria
        
        Format your response as JSON with keys: strategy, position_size, risks, criteria
        """
        
        response = self._query_llm(prompt)
        
        try:
            suggestions = json.loads(response)
        except Exception:
            suggestions = {
                'strategy': 'neutral',
                'position_size': 'moderate',
                'risks': ['Unable to parse LLM response'],
                'criteria': 'Standard technical signals'
            }
        
        logger.info(f"Strategy suggestion: {suggestions.get('strategy', 'unknown')}")
        
        return suggestions
    
    def explain_trade(
        self,
        trade_decision: Dict,
        model_features: Dict,
        outcome: Optional[str] = None
    ) -> str:
        """
        Generate natural language explanation of trade
        
        Args:
            trade_decision: Trade decision details
            model_features: Features used by model
            outcome: Actual outcome (if available)
            
        Returns:
            Human-readable explanation
        """
        prompt = f"""
        Explain this trading decision in simple terms:
        
        Decision: {trade_decision.get('action', 'unknown')} {trade_decision.get('symbol', 'unknown')}
        Size: {trade_decision.get('size', 0)} lots
        Confidence: {trade_decision.get('confidence', 0):.2%}
        
        Key Features:
        {json.dumps(model_features, indent=2)}
        
        {f"Outcome: {outcome}" if outcome else ""}
        
        Provide a clear, concise explanation that a non-technical trader would understand.
        """
        
        response = self._query_llm(prompt)
        
        return response
    
    def assess_risk(
        self,
        proposed_trade: Dict,
        current_positions: List[Dict],
        market_conditions: Dict
    ) -> Dict:
        """
        LLM-based risk assessment
        
        Args:
            proposed_trade: Proposed trade details
            current_positions: Current open positions
            market_conditions: Current market conditions
            
        Returns:
            Risk assessment
        """
        prompt = f"""
        Assess the risk of this proposed trade:
        
        Proposed Trade:
        {json.dumps(proposed_trade, indent=2)}
        
        Current Positions:
        {json.dumps(current_positions, indent=2)}
        
        Market Conditions:
        {json.dumps(market_conditions, indent=2)}
        
        Provide:
        1. Risk level (low/medium/high)
        2. Main risk factors
        3. Recommended adjustments
        4. Should proceed? (yes/no/conditional)
        
        Format as JSON with keys: risk_level, factors, adjustments, proceed
        """
        
        response = self._query_llm(prompt)
        
        try:
            assessment = json.loads(response)
        except Exception:
            assessment = {
                'risk_level': 'medium',
                'factors': ['Unable to parse LLM response'],
                'adjustments': 'Use standard risk management',
                'proceed': 'conditional'
            }
        
        logger.info(f"Risk assessment: {assessment.get('risk_level', 'unknown')}")
        
        return assessment
    
    def generate_market_commentary(
        self,
        daily_performance: Dict,
        significant_events: List[str]
    ) -> str:
        """
        Generate daily market commentary
        
        Args:
            daily_performance: Performance metrics
            significant_events: Significant market events
            
        Returns:
            Market commentary
        """
        prompt = f"""
        Write a brief market commentary for today:
        
        Performance:
        {json.dumps(daily_performance, indent=2)}
        
        Significant Events:
        {chr(10).join(f"- {event}" for event in significant_events)}
        
        Provide a concise, professional summary suitable for a trading report.
        """
        
        response = self._query_llm(prompt)
        
        return response
    
    def _build_market_analysis_prompt(
        self,
        market_data: Dict,
        technical_indicators: Dict,
        news_sentiment: Optional[str]
    ) -> str:
        """Build prompt for market analysis"""
        prompt = f"""
        Analyze current market conditions:
        
        Market Data:
        {json.dumps(market_data, indent=2)}
        
        Technical Indicators:
        {json.dumps(technical_indicators, indent=2)}
        
        {f"News Sentiment: {news_sentiment}" if news_sentiment else ""}
        
        Provide:
        1. Overall market sentiment
        2. Key technical levels
        3. Potential opportunities
        4. Risks to watch
        
        Keep response concise and actionable.
        """
        
        return prompt
    
    def _query_llm(self, prompt: str, max_tokens: int = 500) -> str:
        """Query LLM with prompt"""
        if self.client is None:
            return self._mock_response(prompt)
        try:
        
            if 'gpt' in self.model.lower():
                response = self.client.chat.completions.create(
                    model=self.model,
                    messages=[
                        {"role": "system", "content": "You are an expert trading strategist and market analyst."},
                        {"role": "user", "content": prompt}
                    ],
                    max_tokens=max_tokens,
                    temperature=0.7
                )
                return response.choices[0].message.content
            
            elif 'claude' in self.model.lower():
                response = self.client.messages.create(
                    model=self.model,
                    max_tokens=max_tokens,
                    messages=[
                        {"role": "user", "content": prompt}
                    ]
                )
                return response.content[0].text
        
        except Exception as e:
            logger.error(f"LLM query failed: {e}")
            return self._mock_response(prompt)
    
    def _mock_response(self, prompt: str) -> str:
        """Generate mock response when LLM unavailable"""
        if "analyze" in prompt.lower():
            return """
            Market Analysis:
            1. Overall sentiment: Neutral to slightly bullish
            2. Key levels: Support at 1.0800, Resistance at 1.0900
            3. Opportunities: Potential breakout above resistance
            4. Risks: Watch for reversal signals at resistance
            
            [Mock response - LLM not configured]
            """
        
        elif "strategy" in prompt.lower():
            return json.dumps({
                'strategy': 'neutral',
                'position_size': 'moderate',
                'risks': ['Market volatility', 'News events'],
                'criteria': 'Wait for clear breakout confirmation'
            })
        
        elif "risk" in prompt.lower():
            return json.dumps({
                'risk_level': 'medium',
                'factors': ['Correlation exposure', 'Market volatility'],
                'adjustments': 'Consider reducing position size by 25%',
                'proceed': 'conditional'
            })
        
        else:
            return "Mock LLM response - API not configured"


