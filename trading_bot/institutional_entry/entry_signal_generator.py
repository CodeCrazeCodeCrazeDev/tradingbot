class MultiTimeframeAlignment:
    """
    Analyzes alignment across multiple timeframes for entry confirmation.
    """
    
    def __init__(self, config: Optional[Dict[str, Any]] = None):
        """
        Initialize multi-timeframe alignment analyzer.
        
        Args:
            config: Optional configuration dictionary
        """
        try:
            self.config = config or {}
            self._init_default_config()
        
            logger.info("MultiTimeframeAlignment initialized")
        except Exception as e:
            logger.error(f"Error in __init__: {e}")
            raise
    
    def _init_default_config(self):
        """Initialize default configuration if not provided."""
        try:
            defaults = {
                "timeframes": ["1m", "5m", "15m", "1h", "4h", "1d"],
                "primary_timeframe": "1h",
                "min_aligned_timeframes": 3,
                "trend_sma_period": 50,
                "momentum_rsi_period": 14,
                "momentum_macd_fast": 12,
                "momentum_macd_slow": 26,
                "momentum_macd_signal": 9
            }
        
            for key, value in defaults.items():
                if key not in self.config:
                    self.config[key] = value
        except Exception as e:
            logger.error(f"Error in _init_default_config: {e}")
            raise
    
    def check_alignment(self, 
                      symbol: str, 
                      direction: str, 
                      data: Dict[str, pd.DataFrame]) -> Tuple[bool, Dict[str, bool]]:
        """
        Check if multiple timeframes are aligned with the trade direction.
        
        Args:
            symbol: Symbol to check
            direction: Trade direction ("long" or "short")
            data: Dictionary of timeframe -> OHLCV DataFrame
            
        Returns:
            Tuple of (is_aligned, timeframe_alignment)
        """
        try:
            timeframe_alignment = {}
            aligned_count = 0
        
            for tf in self.config["timeframes"]:
                if tf not in data or data[tf].empty:
                    continue
                
                # Check alignment for this timeframe
                is_aligned = self._check_timeframe_alignment(data[tf], direction)
                timeframe_alignment[tf] = is_aligned
            
                if is_aligned:
                    aligned_count += 1
        
            # Consider aligned if minimum number of timeframes are aligned
            is_aligned = aligned_count >= self.config["min_aligned_timeframes"]
        
            return is_aligned, timeframe_alignment
        except Exception as e:
            logger.error(f"Error in check_alignment: {e}")
            raise
    
    def _check_timeframe_alignment(self, df: pd.DataFrame, direction: str) -> bool:
        """
        Check if a single timeframe is aligned with the trade direction.
        
        Args:
            df: OHLCV DataFrame
            direction: Trade direction ("long" or "short")
            
        Returns:
            True if timeframe is aligned, False otherwise
        """
        try:
            df = df.copy()
        
            # Calculate trend indicators
            df['sma'] = df['close'].rolling(window=self.config["trend_sma_period"]).mean()
        
            # Calculate momentum indicators
            # RSI
            delta = df['close'].diff()
            gain = delta.where(delta > 0, 0)
            loss = -delta.where(delta < 0, 0)
        
            avg_gain = gain.rolling(window=self.config["momentum_rsi_period"]).mean()
            avg_loss = loss.rolling(window=self.config["momentum_rsi_period"]).mean()
        
            rs = avg_gain / avg_loss
            df['rsi'] = 100 - (100 / (1 + rs))
        
            # MACD
            ema_fast = df['close'].ewm(span=self.config["momentum_macd_fast"], adjust=False).mean()
            ema_slow = df['close'].ewm(span=self.config["momentum_macd_slow"], adjust=False).mean()
            df['macd'] = ema_fast - ema_slow
            df['macd_signal'] = df['macd'].ewm(span=self.config["momentum_macd_signal"], adjust=False).mean()
            df['macd_hist'] = df['macd'] - df['macd_signal']
        
            # Get latest values
            if len(df) < self.config["trend_sma_period"]:
                return False
            
            latest = df.iloc[-1]
        
            # Check alignment based on direction
            if direction == "long":
                # Trend alignment
                trend_aligned = latest['close'] > latest['sma']
            
                # Momentum alignment
                rsi_aligned = latest['rsi'] > 50
                macd_aligned = latest['macd'] > latest['macd_signal']
            
                # Consider aligned if trend and at least one momentum indicator are aligned
                return trend_aligned and (rsi_aligned or macd_aligned)
            else:
                # Trend alignment
                trend_aligned = latest['close'] < latest['sma']
            
                # Momentum alignment
                rsi_aligned = latest['rsi'] < 50
                macd_aligned = latest['macd'] < latest['macd_signal']
            
                # Consider aligned if trend and at least one momentum indicator are aligned
                return trend_aligned and (rsi_aligned or macd_aligned)
        except Exception as e:
            logger.error(f"Error in _check_timeframe_alignment: {e}")
            raise


class VolumeConfirmation:
    """
    Analyzes volume patterns for entry confirmation.
    """
    
    def __init__(self, config: Optional[Dict[str, Any]] = None):
        """
        Initialize volume confirmation analyzer.
        
        Args:
            config: Optional configuration dictionary
        """
        try:
            self.config = config or {}
            self._init_default_config()
        
            logger.info("VolumeConfirmation initialized")
        except Exception as e:
            logger.error(f"Error in __init__: {e}")
            raise
    
    def _init_default_config(self):
        """Initialize default configuration if not provided."""
        try:
            defaults = {
                "volume_ma_period": 20,
                "high_volume_threshold": 1.5,  # Multiple of average volume
                "climax_volume_threshold": 2.5,  # Multiple of average volume
                "absorption_threshold": 0.7,  # Ratio of body to range for absorption
                "min_volume_for_valid_signal": 1.0  # Minimum volume multiple for valid signal
            }
        
            for key, value in defaults.items():
                if key not in self.config:
                    self.config[key] = value
        except Exception as e:
            logger.error(f"Error in _init_default_config: {e}")
            raise
    
    def check_volume_confirmation(self, 
                                df: pd.DataFrame, 
                                trigger_time: datetime, 
                                direction: str) -> Tuple[bool, str]:
        """
        Check if volume confirms the entry trigger.
        
        Args:
            df: OHLCV DataFrame
            trigger_time: Time of entry trigger
            direction: Trade direction ("long" or "short")
            
        Returns:
            Tuple of (is_confirmed, reason)
        """
        # Need volume data
        try:
            if 'volume' not in df.columns:
                return False, "No volume data available"
            
            # Find candle closest to trigger time
            closest_idx = df.index.get_indexer([trigger_time], method='nearest')[0]
        
            if closest_idx < 0 or closest_idx >= len(df):
                return False, "Trigger time not found in data"
            
            # Calculate volume metrics
            df = df.copy()
            df['vol_ma'] = df['volume'].rolling(window=self.config["volume_ma_period"]).mean()
            df['rel_volume'] = df['volume'] / df['vol_ma']
        
            # Calculate candle properties
            df['body'] = abs(df['close'] - df['open'])
            df['range'] = df['high'] - df['low']
            df['body_ratio'] = df['body'] / df['range']
            df['is_bullish'] = df['close'] > df['open']
            df['is_bearish'] = df['close'] < df['open']
        
            # Get trigger candle and surrounding candles
            trigger_candle = df.iloc[closest_idx]
            prev_candles = df.iloc[max(0, closest_idx-3):closest_idx]
            next_candles = df.iloc[closest_idx+1:min(closest_idx+4, len(df))]
        
            # Check volume confirmation based on direction
            if direction == "long":
                # For long entries, look for:
                # 1. High volume on trigger candle (especially if bullish)
                # 2. Volume absorption (high volume with small body)
                # 3. Decreasing volume on pullbacks
            
                # Check trigger candle volume
                high_volume = trigger_candle['rel_volume'] >= self.config["high_volume_threshold"]
            
                # Check for volume absorption
                absorption = (trigger_candle['is_bullish'] and 
                             trigger_candle['body_ratio'] >= self.config["absorption_threshold"] and
                             trigger_candle['rel_volume'] >= self.config["min_volume_for_valid_signal"])
            
                # Check for decreasing volume on pullbacks in next candles
                decreasing_pullback_volume = False
                if len(next_candles) >= 2:
                    pullbacks = next_candles[next_candles['is_bearish'] == True]
                    if not pullbacks.empty:
                        decreasing_pullback_volume = all(
                            pullbacks['rel_volume'].iloc[i] < pullbacks['rel_volume'].iloc[i-1] 
                            for i in range(1, len(pullbacks))
                        )
            
                # Determine confirmation
                if high_volume and trigger_candle['is_bullish']:
                    return True, "High volume bullish candle"
                elif absorption:
                    return True, "Volume absorption detected"
                elif decreasing_pullback_volume:
                    return True, "Decreasing volume on pullbacks"
                elif trigger_candle['rel_volume'] >= self.config["min_volume_for_valid_signal"]:
                    return True, "Sufficient volume for entry"
                else:
                    return False, "Insufficient volume confirmation"
            else:
                # For short entries, look for:
                # 1. High volume on trigger candle (especially if bearish)
                # 2. Volume absorption (high volume with small body)
                # 3. Decreasing volume on bounces
            
                # Check trigger candle volume
                high_volume = trigger_candle['rel_volume'] >= self.config["high_volume_threshold"]
            
                # Check for volume absorption
                absorption = (trigger_candle['is_bearish'] and 
                             trigger_candle['body_ratio'] >= self.config["absorption_threshold"] and
                             trigger_candle['rel_volume'] >= self.config["min_volume_for_valid_signal"])
            
                # Check for decreasing volume on bounces in next candles
                decreasing_bounce_volume = False
                if len(next_candles) >= 2:
                    bounces = next_candles[next_candles['is_bullish'] == True]
                    if not bounces.empty:
                        decreasing_bounce_volume = all(
                            bounces['rel_volume'].iloc[i] < bounces['rel_volume'].iloc[i-1] 
                            for i in range(1, len(bounces))
                        )
            
                # Determine confirmation
                if high_volume and trigger_candle['is_bearish']:
                    return True, "High volume bearish candle"
                elif absorption:
                    return True, "Volume absorption detected"
                elif decreasing_bounce_volume:
                    return True, "Decreasing volume on bounces"
                elif trigger_candle['rel_volume'] >= self.config["min_volume_for_valid_signal"]:
                    return True, "Sufficient volume for entry"
                else:
                    return False, "Insufficient volume confirmation"
        except Exception as e:
            logger.error(f"Error in check_volume_confirmation: {e}")
            raise


class EntrySignalGenerator:
    """
    Generates high-probability entry signals based on institutional analysis.
    """
    
    def __init__(self, config: Optional[Dict[str, Any]] = None):
        """
        Initialize entry signal generator.
        
        Args:
            config: Optional configuration dictionary
        """
        try:
            self.config = config or {}
            self._init_default_config()
        
            # Initialize components
            self.entry_validator = EntryValidator(self.config.get("entry_validator_config"))
            self.mtf_alignment = MultiTimeframeAlignment(self.config.get("mtf_alignment_config"))
            self.volume_confirmation = VolumeConfirmation(self.config.get("volume_confirmation_config"))
        
            # Signal history
            self.signals: Dict[str, List[EntrySignal]] = {}  # symbol -> signals
        
            logger.info("EntrySignalGenerator initialized")
        except Exception as e:
            logger.error(f"Error in __init__: {e}")
            raise
    
    def _init_default_config(self):
        """Initialize default configuration if not provided."""
        try:
            defaults = {
                "signal_expiration_hours": 24,
                "default_risk_reward_ratio": 2.0,
                "min_risk_reward_ratio": 1.5,
                "default_stop_loss_pct": 1.0,  # 1% default stop loss
                "default_take_profit_pct": 2.0,  # 2% default take profit
                "confidence_threshold_very_high": 0.9,
                "confidence_threshold_high": 0.75,
                "confidence_threshold_medium": 0.6,
                "confidence_threshold_low": 0.4,
                "strength_threshold_very_strong": 0.9,
                "strength_threshold_strong": 0.75,
                "strength_threshold_moderate": 0.6,
                "strength_threshold_weak": 0.4
            }
        
            for key, value in defaults.items():
                if key not in self.config:
                    self.config[key] = value
        except Exception as e:
            logger.error(f"Error in _init_default_config: {e}")
            raise
    
    def generate_signals(self, 
                       triggers: List[EntryTrigger], 
                       data: Dict[str, pd.DataFrame]) -> List[EntrySignal]:
        """
        Generate entry signals from triggers.
        
        Args:
            triggers: List of entry triggers
            data: Dictionary of timeframe -> OHLCV DataFrame
            
        Returns:
            List of generated entry signals
        """
        try:
            signals = []
        
            for trigger in triggers:
                # Validate trigger
                is_valid, reason = self.entry_validator.validate_entry(trigger, data)
            
                if not is_valid:
                    logger.info(f"Entry trigger {trigger.id} invalid: {reason}")
                    continue
            
                # Generate signal
                signal = self._create_signal_from_trigger(trigger, data)
            
                if signal:
                    signals.append(signal)
                
                    # Add to history
                    if trigger.symbol not in self.signals:
                        self.signals[trigger.symbol] = []
                    self.signals[trigger.symbol].append(signal)
        
            return signals
        except Exception as e:
            logger.error(f"Error in generate_signals: {e}")
            raise
    
    def _create_signal_from_trigger(self, 
                                  trigger: EntryTrigger, 
                                  data: Dict[str, pd.DataFrame]) -> Optional[EntrySignal]:
        """
        Create entry signal from trigger.
        
        Args:
            trigger: Entry trigger
            data: Dictionary of timeframe -> OHLCV DataFrame
            
        Returns:
            Entry signal or None if invalid
        """
        # Check if we have data for trigger timeframe
        try:
            if trigger.timeframe not in data or data[trigger.timeframe].empty:
                logger.warning(f"No data for timeframe {trigger.timeframe}")
                return None
            
            df = data[trigger.timeframe]
        
            # Calculate signal strength
            strength = self._calculate_signal_strength(trigger)
        
            # Calculate signal confidence
            confidence = self._calculate_signal_confidence(trigger)
        
            # Calculate stop loss and take profit
            stop_loss, take_profit = self._calculate_risk_levels(trigger, df)
        
            # Calculate risk-reward ratio
            risk_reward_ratio = None
            if stop_loss is not None and take_profit is not None and trigger.price != stop_loss:
                if trigger.direction == "long":
                    risk = trigger.price - stop_loss
                    reward = take_profit - trigger.price
                else:
                    risk = stop_loss - trigger.price
                    reward = trigger.price - take_profit
                
                if risk > 0:
                    risk_reward_ratio = reward / risk
        
            # Create signal
            signal_id = f"signal_{uuid.uuid4().hex[:8]}"
        
            # Calculate expiration
            expiration = trigger.timestamp + pd.Timedelta(hours=self.config["signal_expiration_hours"])
        
            signal = EntrySignal(
                id=signal_id,
                symbol=trigger.symbol,
                timestamp=trigger.timestamp,
                entry_type=trigger.entry_type,
                price=trigger.price,
                direction=trigger.direction,
                timeframe=trigger.timeframe,
                strength=strength,
                confidence=confidence,
                stop_loss=stop_loss,
                take_profit=take_profit,
                risk_reward_ratio=risk_reward_ratio,
                trigger_id=trigger.id,
                confirmations=trigger.confirmations.copy(),
                notes=trigger.notes,
                expiration=expiration
            )
        
            return signal
        except Exception as e:
            logger.error(f"Error in _create_signal_from_trigger: {e}")
            raise
    
    def _calculate_signal_strength(self, trigger: EntryTrigger) -> SignalStrength:
        """
        Calculate signal strength based on trigger properties.
        
        Args:
            trigger: Entry trigger
            
        Returns:
            Signal strength
        """
        # Calculate base strength from confidence
        try:
            base_strength = trigger.confidence
        
            # Adjust based on confirmations
            confirmation_bonus = min(0.3, len(trigger.confirmations) * 0.1)
        
            # Adjust based on specific confirmations
            if EntryConfirmation.VOLUME_CONFIRMATION in trigger.confirmations:
                base_strength += 0.05
            
            if EntryConfirmation.MULTI_TIMEFRAME_ALIGNMENT in trigger.confirmations:
                base_strength += 0.1
            
            if EntryConfirmation.ORDER_FLOW_ABSORPTION in trigger.confirmations:
                base_strength += 0.1
            
            # Calculate final strength
            final_strength = min(1.0, base_strength + confirmation_bonus)
        
            # Map to strength enum
            if final_strength >= self.config["strength_threshold_very_strong"]:
                return SignalStrength.VERY_STRONG
            elif final_strength >= self.config["strength_threshold_strong"]:
                return SignalStrength.STRONG
            elif final_strength >= self.config["strength_threshold_moderate"]:
                return SignalStrength.MODERATE
            elif final_strength >= self.config["strength_threshold_weak"]:
                return SignalStrength.WEAK
            else:
                return SignalStrength.VERY_WEAK
        except Exception as e:
            logger.error(f"Error in _calculate_signal_strength: {e}")
            raise
    
    def _calculate_signal_confidence(self, trigger: EntryTrigger) -> SignalConfidence:
        """
        Calculate signal confidence based on trigger properties.
        
        Args:
            trigger: Entry trigger
            
        Returns:
            Signal confidence
        """
        # Use trigger confidence directly
        try:
            confidence = trigger.confidence
        
            # Map to confidence enum
            if confidence >= self.config["confidence_threshold_very_high"]:
                return SignalConfidence.VERY_HIGH
            elif confidence >= self.config["confidence_threshold_high"]:
                return SignalConfidence.HIGH
            elif confidence >= self.config["confidence_threshold_medium"]:
                return SignalConfidence.MEDIUM
            elif confidence >= self.config["confidence_threshold_low"]:
                return SignalConfidence.LOW
            else:
                return SignalConfidence.VERY_LOW
        except Exception as e:
            logger.error(f"Error in _calculate_signal_confidence: {e}")
            raise
    
    def _calculate_risk_levels(self, 
                             trigger: EntryTrigger, 
                             df: pd.DataFrame) -> Tuple[float, Optional[float]]:
        """
        Calculate stop loss and take profit levels.
        
        Args:
            trigger: Entry trigger
            df: OHLCV DataFrame
            
        Returns:
            Tuple of (stop_loss, take_profit)
        """
        # Use trigger stop loss if provided
        try:
            if trigger.stop_loss is not None:
                stop_loss = trigger.stop_loss
            else:
                # Calculate default stop loss
                stop_loss_pct = self.config["default_stop_loss_pct"] / 100
            
                if trigger.direction == "long":
                    stop_loss = trigger.price * (1 - stop_loss_pct)
                else:
                    stop_loss = trigger.price * (1 + stop_loss_pct)
        
            # Use trigger take profit if provided
            if trigger.take_profit is not None:
                take_profit = trigger.take_profit
            else:
                # Calculate default take profit based on risk-reward ratio
                risk_reward = self.config["default_risk_reward_ratio"]
            
                if trigger.direction == "long":
                    risk = trigger.price - stop_loss
                    take_profit = trigger.price + (risk * risk_reward)
                else:
                    risk = stop_loss - trigger.price
                    take_profit = trigger.price - (risk * risk_reward)
        
            return stop_loss, take_profit
        except Exception as e:
            logger.error(f"Error in _calculate_risk_levels: {e}")
            raise
    
    def update_signal_status(self, 
                           signal: EntrySignal, 
                           current_price: float, 
                           current_time: datetime) -> EntrySignal:
        """
        Update signal status based on current price and time.
        
        Args:
            signal: Entry signal to update
            current_price: Current price
            current_time: Current time
            
        Returns:
            Updated entry signal
        """
        # Check if signal has expired
        try:
            if signal.expiration and current_time > signal.expiration:
                signal.notes += " [EXPIRED]"
                return signal
            
            # Check if signal has been executed
            if not signal.executed:
                # Check if price has reached entry level
                if signal.direction == "long" and current_price <= signal.price:
                    signal.executed = True
                    signal.execution_price = current_price
                    signal.execution_time = current_time
                    signal.notes += f" [EXECUTED at {current_price:.5f}]"
                elif signal.direction == "short" and current_price >= signal.price:
                    signal.executed = True
                    signal.execution_price = current_price
                    signal.execution_time = current_time
                    signal.notes += f" [EXECUTED at {current_price:.5f}]"
        
            return signal
        except Exception as e:
            logger.error(f"Error in update_signal_status: {e}")
            raise
    
    def get_active_signals(self, symbol: str, current_time: datetime) -> List[EntrySignal]:
        """
        Get active (non-expired, non-executed) signals for a symbol.
        
        Args:
            symbol: Symbol to get signals for
            current_time: Current time
            
        Returns:
            List of active signals
        """
        try:
            if symbol not in self.signals:
                return []
            
            return [
                signal for signal in self.signals[symbol]
                if (not signal.expiration or current_time <= signal.expiration) and not signal.executed
            ]
        except Exception as e:
            logger.error(f"Error in get_active_signals: {e}")
            raise
    
    def get_executed_signals(self, symbol: str) -> List[EntrySignal]:
        """
        Get executed signals for a symbol.
        
        Args:
            symbol: Symbol to get signals for
            
        Returns:
            List of executed signals
        """
        try:
            if symbol not in self.signals:
                return []
            
            return [signal for signal in self.signals[symbol] if signal.executed]
        except Exception as e:
            logger.error(f"Error in get_executed_signals: {e}")
            raise
