class SignalStrength(enum.Enum):
    """Strength levels for entry signals."""
    VERY_STRONG = "very_strong"  # Very strong signal (multiple confirmations)
    STRONG = "strong"            # Strong signal (all confirmations)
    MODERATE = "moderate"        # Moderate signal (most confirmations)
    WEAK = "weak"                # Weak signal (minimum confirmations)
    VERY_WEAK = "very_weak"      # Very weak signal (below minimum confirmations)


class SignalConfidence(enum.Enum):
    """Confidence levels for entry signals."""
    VERY_HIGH = "very_high"      # Very high confidence (>90%)
    HIGH = "high"                # High confidence (75-90%)
    MEDIUM = "medium"            # Medium confidence (60-75%)
    LOW = "low"                  # Low confidence (40-60%)
    VERY_LOW = "very_low"        # Very low confidence (<40%)


